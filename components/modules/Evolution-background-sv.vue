            <section id="s4">
                <h2 id="s4t">
                    Felaktiga uppfattningar
                </h2>

                <p>
                    Ibland hamnar lärare i mottagandet av frågor om inkludering av evolution i läroplanen. Eleverna kan ta upp utmaningar i klassen. Föräldrar kan invända mot att deras barn lär sig om evolution. Skoladministratörer eller andra lärare kan misslyckas med att stödja lärare i deras ansträngningar att undervisa i evolution. Gemenskapsmedlemmar kan presentera avsiktliga utmaningar för enskilda lärare, skoldistrikt eller till och med statliga enheter genom att försöka påverka vetenskapsundervisningsstandarder eller genom att ändra lagstiftning.
                </p>
                <p>
                    Alla lärare, även de i samhällen som grundligt stödjer undervisning i evolution, bör komma ihåg att vissa elever uppfattar evolution som oförenlig med religiös tro. Även om många religiösa åsikter är förenliga med evolutionsteorin och även om många religiösa organisationer stödjer undervisningen i evolution, kan eleverna vara omedvetna om dessa fakta.
                </p>
                <p>
                    Uppfattningen om en krock mellan vetenskap och elevers övertygelse kan orsaka obehag i klassen. För att göra dessa elever mer bekväma kan lärare hjälpa dem att förstå att evolution, liksom all vetenskap, försöker förklara naturliga saker genom naturliga orsaker. Det behöver inte anses vara oförenligt med deras tro eftersom vetenskapen inte förlitar sig på, och inte kan utvärdera eller testa, övernaturliga förklaringar. Samtidigt bör din undervisning spegla det faktum att evolution är den enda vetenskapligt giltiga och accepterade teorin som förklarar våra observationer av den biologiska världen. Alternativa "teorier" som har föreslagits för att infogas i den naturvetenskapliga läroplanen har inte fått stöd av giltig vetenskap.
                </p>
                <p>
                    Nedan presenteras några av de vanliga missuppfattningarna om evolution och hur lärare kan hantera dem.
                </p>

                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 1:
                    </span>
                    Evolution är en teori om livets ursprung.
                </h3>
                <p>
                    Evolutionsteorin omfattar idéer och bevis angående livets ursprung (t.ex. om det hände nära ett djuphavshål eller inte, vilka organiska molekyler som kom först, etc.), men detta är inte det centrala fokuset för evolutionsteorin. Det mesta av evolutionsbiologin handlar om hur livet förändrades efter dess uppkomst. Oavsett hur livet började förgrenades det efteråt och diversifierades, och de flesta studier av evolution är fokuserade på dessa processer.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 2:
                    </span>
                    Evolutionsteorin antyder att livet utvecklades (och fortsätter att utvecklas) slumpmässigt eller av en slump.
                </h3>
                <p>
                    Vissa viktiga evolutionsmekanismer är icke-slumpmässiga och dessa gör den övergripande processen icke-slumpmässig.
                </p>
                <p>
                    Till exempel resulterar naturligt urval i anpassningar (t.ex. fladdermössens förmåga att ekolokalisera). Sådana fantastiska anpassningar kom uppenbarligen inte till "av en slump". De utvecklades via en kombination av slumpmässiga och icke-slumpmässiga processer.
                </p>
                <p>
                    Mutationsprocessen, som genererar genetisk variation, är slumpmässig, men urvalet är icke-slumpmässigt. Urvalet gynnade varianter som bättre kunde överleva och reproducera sig (t.ex. att navigera i mörkret). Under många generationer av slumpmässig mutation och icke-slumpmässigt urval utvecklades komplexa anpassningar.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 3:
                    </span>
                    Enskilda organismer kan utvecklas genom mutationer under en enda livslängd.
                </h3>
                <p>
                    Evolutionär förändring är baserad på förändringar i den genetiska sammansättningen av populationer över tid. Populationer, inte enskilda organismer, utvecklas genom mutationer. Nya genvarianter (dvs alleler) produceras genom slumpmässig mutation, och under loppet av många generationer kan naturligt urval gynna fördelaktiga varianter, vilket gör att de blir vanligare i befolkningen.
                </p>
                

                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 4:
                    </span>
                    Eftersom evolutionen går långsamt kan människor inte påverka den.
                </h3>
                <p>
                    Eftersom människor ofta orsakar stora förändringar i miljön, är vi ofta initiativtagare till evolution i andra organismer. Några exempel på mänskligt orsakad evolution:
                </p>
                <ul>
                    <li>
                        Insekter som vägglöss och växtskadegörare har utvecklat resistens mot bekämpningsmedel.
                    </li>
                    <li>
                        Bakterier, HIV, malaria och cancer har utvecklat resistens mot droger.
                    </li>
                </ul>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 5:
                    </span>
                    Människor utvecklas inte för närvarande.
                </h3>
                <p>
                    Människor kan nu modifiera våra miljöer med teknik. Vi har uppfunnit medicinska behandlingar, jordbruksmetoder och ekonomiska strukturer som väsentligt förändrar utmaningarna för reproduktion och överlevnad som moderna människor står inför. Så, till exempel, eftersom vi nu kan behandla diabetes med insulin, är de genversioner som bidrar till ungdomsdiabetes inte längre starkt utvalda i utvecklade länder. Vissa har hävdat att sådana tekniska framsteg gör att vi har slutat utvecklas.
                </p>
                <p>
                    Så är dock inte fallet. Människor står fortfarande inför utmaningar för överlevnad och reproduktion, bara inte samma som vi gjorde för 20 000 år sedan. Riktningen, men inte faktumet av vår evolution har förändrats. Till exempel, moderna människor som bor i tätbefolkade områden står inför större risker för epidemiska sjukdomar än våra jägare-samlare förfäder (som inte kom i nära kontakt med så många människor dagligen) - och denna situation gynnar spridningen av gener versioner som skyddar mot dessa sjukdomar. Forskare har upptäckt många sådana fall av den senaste mänskliga evolutionen. Utforska dessa länkar för att lära dig mer om:
                </p>
                <ul>
                    <li>
                        <a href="https://evolution.berkeley.edu/evo-news/evolving-altitude-aptitude/" target="_blank" rel="noopener noreferrer">
                            Utveckling av höjdbegåvning
                        </a>
                    </li>
                    <li>
                        <a href="https://evolution.berkeley.edu/evo-news/ghosts-of-epidemics-past/" target="_blank" rel="noopener noreferrer">
                            Spöken av epidemier tidigare - utveckling av mänskliga genetiska egenskaper som skyddar mot malaria
                        </a>
                    </li>
                    <li>
                        <a href="https://evolution.berkeley.edu/evo-news/got-lactase/" target="_blank" rel="noopener noreferrer">
                            Har du laktas? - utveckling av laktostolerans hos människor
                        </a>
                    </li>
                </ul>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 6:
                    </span>
                    Naturligt urval involverar organismer som försöker anpassa sig.
                </h3>
                <p>
                    Naturligt urval leder till anpassning av arter över tid, men processen innebär inte ansträngning, försök eller vilja. Naturligt urval är ett naturligt resultat av genetisk variation i en population och det faktum att vissa av dessa varianter kanske kan lämna fler avkommor i nästa generation än andra varianter. Den genetiska variationen genereras av slumpmässig mutation - en process som är opåverkad av vad organismer i befolkningen vill ha eller vad de "försöker" göra. Antingen har en individ gener som är tillräckligt bra för att överleva och föröka sig, eller så har den inte; den kan inte få rätt gener genom att "försöka".
                </p>
                <p>
                    Till exempel utvecklar bakterier inte resistens mot våra antibiotika eftersom de "försöker" så mycket. Istället utvecklas resistens eftersom slumpmässig mutation råkar generera vissa individer som bättre kan överleva antibiotikan, och dessa individer kan reproducera sig mer än andra och lämnar efter sig mer resistenta bakterier.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 7:
                    </span>
                    Människor kan inte påverka ekosystemen negativt, eftersom arter bara kommer att utvecklas vad de behöver för att överleva.
                </h3>
                <p>
                    Naturligt urval ger inte automatiskt organismer de egenskaper de "behöver" för att överleva. Naturligtvis kan vissa arter ha egenskaper som gör att de kan frodas under förhållanden av miljöförändringar orsakade av människor och därför kan väljas ut för, men andra kanske inte och därför kan dö ut.
                </p>
                <p>
                    Om en population eller art inte råkar ha rätt sorts genetisk variation, kommer den inte att utvecklas som svar på de miljöförändringar som orsakas av människor, oavsett om dessa förändringar orsakas av föroreningar, klimatförändringar, intrång i livsmiljöer eller andra faktorer.
                </p>
                <p>
                    Till exempel, eftersom klimatförändringarna gör att isen i Arktiska havet förtunnas och bryts upp allt tidigare, får isbjörnar svårare att få mat. Om isbjörnspopulationer inte har den genetiska variationen som skulle tillåta vissa individer att dra nytta av jaktmöjligheter som inte är beroende av havsis, kan de dö ut i naturen.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 8:
                    </span>
                    De starkaste organiska smsen i en befolkning är de som är starkast, friskast, snabbast och/eller störst.
                </h3>
                <p>
                    I evolutionära termer har fitness en helt annan betydelse än ordets vardagliga betydelse. En organisms evolutionära kondition indikerar inte dess hälsa, utan snarare dess förmåga att få in sina gener till nästa generation. Ju mer fertil avkomma en organism lämnar i nästa generation, desto bättre är den. Detta korrelerar inte alltid med styrka, hastighet eller storlek.
                </p>
                <p>
                    Till exempel kan en liten hanfågel med ljusa stjärtfjädrar lämna efter sig fler avkommor än en starkare, mattare hane, och en spinkig växt med stora fröskidor kan lämna efter sig fler avkommor än ett större exemplar - vilket betyder att den ynka fågeln och den spinkiga plantan har högre evolutionär kondition än sina starkare, större motsvarigheter.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 9:
                    </span>
                    Evolution är inte vetenskap eftersom den inte är observerbar eller testbar.
                </h3>
                <p>
                    Denna missuppfattning omfattar två felaktiga idéer:
                </p>
                <ul>
                    <li>
                        att all vetenskap är beroende av kontrollerade laboratorieexperiment, och
                    </li>
                    <li>
                        att evolutionen inte kan studeras med sådana experiment.
                    </li>
                </ul>
                <p>
                    För det första involverar många vetenskapliga undersökningar inte experiment eller direkt observation. Astronomer kan inte hålla stjärnor i sina händer och geologer kan inte gå tillbaka i tiden, men båda forskarna kan lära sig mycket om universum genom observation och jämförelse. På samma sätt kan evolutionsbiologer testa sina idéer om livets historia på jorden genom att göra observationer i den verkliga världen.
                </p>
                <p>
                    För det andra, även om vi inte kan köra ett experiment som kommer att berätta för oss hur dinosaurielinjen strålade, kan vi studera många aspekter av evolutionen med kontrollerade experiment i laboratoriemiljö. I organismer med korta generationstider (t.ex. bakterier eller fruktflugor) kan vi faktiskt observera evolution i aktion under loppet av ett experiment. <a href="https://www.youtube.com/watch?v=plVk4NVIUh8" target="_blank" rel="noopener noreferrer">Bakterieutvecklingen på en "megaplatta" petriskål</a>. I vissa fall har biologer observerat evolution som sker i naturen.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 10:
                    </span>
                    Evolutionärt är teorin ogiltig eftersom den är ofullständig och inte kan ge en total förklaring till den biologiska mångfalden vi ser omkring oss.
                </h3>
                <p>
                    Denna missuppfattning härrör från en missuppfattning om naturvetenskapliga teoriers natur. Alla vetenskapliga teorier (från evolutionsteori till atomteori) är pågående arbeten. När nya bevis upptäcks och nya idéer utvecklas förändras vår förståelse av hur världen fungerar och det gör även vetenskapliga teorier. Även om vi inte vet allt som finns att veta om evolution (eller någon annan vetenskaplig disciplin, för den delen), vet vi en hel del om livets historia, mönstret av släktuppdelning genom tiden och mekanismerna som har orsakat dessa förändringar. Och mer kommer att lära sig i framtiden.
                </p>
                <p>
                    Evolutionsteori, precis som vilken vetenskaplig teori som helst, förklarar ännu inte allt vi observerar i den naturliga världen. Evolutionsteorin hjälper oss dock att förstå ett brett spektrum av observationer (från uppkomsten av antibiotikaresistenta bakterier till den fysiska matchningen mellan pollinatörer och deras föredragna blommor), gör korrekta förutsägelser i nya situationer (t.ex. att behandling av AIDS-patienter med en cocktail av mediciner bör bromsa utvecklingen av viruset), och har bevisat sig gång på gång i tusentals experiment och observationsstudier. Än så länge är evolutionen den enda väl underbyggda förklaringen till livets mångfald.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 11:
                    </span>
                    Luckor i fossilregistret motbevisar evolutionen.
                </h3>
                <p>
                    Även om det är sant att det finns luckor i fossilregistret, utgör detta inte bevis mot evolutionsteorin. Forskare utvärderar hypoteser och teorier genom att ta reda på vad vi skulle förvänta oss att observera om en viss idé var sann och sedan se om dessa förväntningar stämmer. Om evolutionsteorin var sann, skulle vi förvänta oss att det skulle ha funnits övergångsformer som förbinder gamla arter med deras förfäder och ättlingar.
                </p>
                <p>
                    Denna förväntning har infriats. Paleontologer har hittat många fossil med övergångsegenskaper, och nya fossil upptäcks hela tiden. Men om evolutionsteorin vore sann, skulle vi inte förvänta oss att alla dessa former skulle bevaras i fossilregistret. Många organismer har inga kroppsdelar som fossiliserar bra, miljöförhållandena för att bilda bra fossiler är sällsynta, och naturligtvis har vi bara upptäckt en liten andel av fossilerna som kan bevaras någonstans på jorden. Så forskare förväntar sig att det för många evolutionära övergångar kommer att finnas luckor i fossilregistret.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 12:
                    </span>
                    Evolution leder till omoraliskt beteende.
                </h3>
                <p>
                    Evolution gör inga etiska uttalanden om rätt och fel. Vissa människor misstolkar det faktum att evolutionen har format djurens beteende (inklusive mänskligt beteende) som stöd för idén att alla beteenden som är "naturliga" är de "rätta". Så är inte fallet. Det är upp till oss, som samhällen och individer, att bestämma vad som är etiskt och moraliskt beteende. Evolution hjälper oss helt enkelt att förstå hur livet har förändrats och fortsätter att förändras över tid - och berättar inte för oss om dessa processer eller resultaten av dem är "rätt" eller "fel". Dessutom tror vissa människor felaktigt att evolution och religiös tro är oförenliga och antar därför att accepterande av evolutionsteori uppmuntrar omoraliskt beteende. Inte heller är korrekta.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 13:
                    </span>
                    Evolution och religion är oförenliga.
                </h3>
                <p>
                    På grund av att vissa individer och grupper ivrigt deklarerar sin tro är det lätt att få intrycket att vetenskap (som inkluderar evolution) och religion är i krig; Men tanken att man alltid måste välja mellan vetenskap och religion är felaktig. Människor med många olika trosuppfattningar och nivåer av vetenskaplig expertis ser ingen som helst motsättning mellan vetenskap och religion. För många av dessa människor handlar vetenskap och religion helt enkelt om olika världar. Vetenskapen handlar om naturliga orsaker till naturfenomen, medan religion handlar om föreställningar som ligger bortom den naturliga världen. Naturligtvis motsäger vissa religiösa övertygelser uttryckligen vetenskapen (t.ex. tron att världen och allt liv på den skapades på sex bokstavliga dagar står i konflikt med evolutionsteorin); dock har de flesta religiösa grupper ingen konflikt med evolutionsteorin eller andra vetenskapliga rön. Faktum är att många religiösa människor, inklusive teologer, känner att en djupare förståelse av naturen faktiskt berikar deras tro. Dessutom finns det i det vetenskapliga samfundet tusentals vetenskapsmän som är andäktigt religiösa och som också accepterar evolutionen.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 14:
                    </span>
                    Lärare bör undervisa "båda sidor" av evolutionsfrågan och låta eleverna bestämma. Eller så borde lärare ge lika tid åt evolution och kreationism.
                </h3>
                <p>
                    Lika tid är inte meningsfullt när de två "sidorna" inte är lika. Religion och vetenskap är väldigt olika strävanden, och religiösa åsikter hör inte alls hemma i ett naturvetenskapligt klassrum. I naturvetenskapsklassen ska eleverna ha möjligheter att diskutera fördelarna med argument och bevis inom vetenskapens ram. Eleverna kan till exempel undersöka och diskutera exakt var fåglarna förgrenade sig från ett fylogenetiskt träd (släktskapsträd): före dinosaurier eller inifrån dinosauriekladden. Däremot har en debatt som ställer ett vetenskapligt begrepp mot en religiös övertygelse ingen plats i en naturvetenskaplig klass och antyder vilseledande att ett "val" mellan de två måste göras. Argumentet "rättvisa" har använts av grupper som försöker insinuera sin religiösa övertygelse i naturvetenskapliga läroplaner.
                </p>
                
                <hr />
                <h3>
                    <span class="text-rose-400">
                        Missuppfattning 15:
                    </span>
                    Evolution är i sig religiöst, så att kräva att lärare undervisar i evolution bryter mot det första tillägget.
                </h3>
                <p>
                    Detta felaktiga argument är baserat på idén att evolution och religion i grunden är desamma eftersom de båda är "trossystem". Denna idé är helt enkelt felaktig. Tron på religiösa idéer är baserad på tro, och religion behandlar ämnen utanför den naturliga världens område. Acceptans av vetenskapliga idéer (som evolution) baseras på bevis från den naturliga världen, och vetenskapen är begränsad till att studera fenomen och processer i den naturliga världen. Högsta domstolens och andra federala domstolsbeslut skiljer tydligt vetenskap från religion och tillåter inte förespråkande av religiös doktrin i vetenskapsklasser (eller andra offentliga skolor). Andra beslut upprätthåller specifikt ett skoldistrikts rätt att kräva undervisning i evolution.
                </p>
            </section>


