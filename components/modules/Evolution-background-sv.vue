                <h2 id="s1t">Vad är evolution?</h2>
                <p>
                    <a href="https://www.nrm.se/faktaomnaturenochrymden/evolution.1298.html" target="_blank" rel="noopener noreferrer"><span class="e-term">Evolution</span></a> syftar på de processer, mer eller mindre gradvisa, som har förvandlat livet på jorden från dess tidigaste former till den stora mångfald som kännetecknar det idag. De första primitiva levande organismerna dateras så långt tillbaka som 3800 miljoner år sedan. Faktum är att allt i universum utvecklas, ingenting står stilla. Vi kan prata om evolutionen av en stjärna, evolutionen av litosfären ... och evolutionen av levande organismer. Det verkar uppenbart att levande varelser har dykt upp och försvunnit under geologisk tid, och att många arter, nuförtiden med olika egenskaper, har gemensamma förfäder. Det är känt att det finns arter som funnits i tidigare tider och nu är utrotade, och andra som inte existerade i äldre geologisk tid men som finns idag. Generellt sett är evolutionär förändring baserad på interaktionen mellan populationer av organismer och deras miljö. Eftersom det har skett förändringar i miljön har olika egenskaper behövts för att anpassa sig till sådana förändringar.
