                <h2 id="s2t">
                    Evolutionsteorier
                </h2>
                <h3>
                    Lamarcks evolutionsteori
                </h3>
                <ol>
                    Lamarcks teori bygger på:
                    <li>
                        Evolution drevs av en medfödd tendens mot högre och större komplexitet, mot perfektion. (livskraft).
                    </li>
                    <li>
                        Denna medfödda tendens till perfektion påverkas av miljöförändringar och blir därför bättre anpassade till deras miljöer. Dessa miljöförändringar skulle skapa "nya behov" hos organismer, som behöver använda vissa organ eller egenskaper mer än andra.
                    </li>
                    <li>
                        Denna användning och obruk av vissa organ gör att de organ som används oftare klarar sig bättre med miljön, vilket gör att de blir större och starkare, medan de som inte används försämras. Med andra ord: funktionen ”skapar” organen. Så organismer kan förvärva vissa egenskaper under sin livstid och förlora andra.
                    </li>
                    <li>
                        Lamarck trodde att de modifikationer som en organism förvärvar under sin livstid överförs till dess avkomma (arv av förvärvade egenskaper).
                    </li>
                    <li>
                        En art kan inte förvandlas till någon annan art, utan bara till en liknande där arten kan utvecklas gradvis. Lamarck fastställde evolutionens faktum, men hans teori hade flera brister:
                        <ul>
                            <li>
                                Det finns inga bevis som bevisar den medfödda tendensen (vital impuls) hos organismer mot komplexitet.
                            </li>
                            <li>
                                Förvärvade egenskaper ärvs inte. Vi vet alla att fysisk träning kommer att få våra muskler att utvecklas, men sådana förvärvade fysiska egenskaper (som inte är genetiska) överförs inte till avkomman. Sådana förvärvade egenskaper produceras av miljöfaktorer och utveckling, men inte av gener. Endast de karaktärsdrag som regleras av gener är ärftliga, och endast om sådana gener finns i reproduktionscellerna. Vad som än händer med somatiska celler på grund av att de används eller inte används kommer inte att påverka generna i könscellerna, därför kommer det inte att vara ärftligt. August Weissmann (1834-1914) genomförde följande experiment för att motbevisa Lamarcks teori: Han skar av svansen på femtonhundra möss, upprepade gånger under 20 generationer, och rapporterade att ingen mus någonsin föddes utan svans. Enligt Lamarck och hans teori borde avskaffandet av svansen ha gett avkomma utan svans eller med outvecklad svans, dock var det aldrig fallet.
                            </li>
                        </ul>
                    </li>
                </ol>

                <h3>
                    Darwin-Wallaces evolutionsteorin
                </h3>
                <p>
                    Charles Darwin var 22 år när han seglade från England med Beagle i december 1831; en resa jorden runt som skulle pågå i fem år. Under expeditionen samlade Darwin in tusentals exemplar av exotiska och oerhört olika fauna och floror på olika platser. Han kunde också observera de olika anpassningarna av växter och djur som bebodde så olika miljöer.
                </p>
                <ul>
                    Även om Darwin inte visste något om arvsmekanismen, var han mycket medveten om många andra aspekter av levande organismer. Bland dessa betonas särskilt:
                    <li>
                        De arter som lever på jorden idag är inte samma arter som fanns förr, även om de liknar dem. Denna aspekt av evolutionen var mycket uppenbar för Darwin från studier av fossiler.
                    </li>
                    <li>
                        Varje art har ett antal karaktärsdrag som anpassar individer inom den arten till deras sätt att leva och deras speciella miljö. Mycket av The Origin of Species ägnas åt detaljerade beskrivningar av individuella arters anpassningar, till exempel de olika näbbformerna hos finkar på Galapagosöarna.
                    </li>
                    <li>
                        Selektiv förädling av inhemska arter kan producera karaktärsdrag i en mängd olika former. Till exempel har hunduppfödare producerat många raser som skiljer sig åt i egenskaper som öron längd, resning och beteende.
                    </li>
                </ul>
                <p>
                    En annan naturforskare, Alfred Wallace som arbetade i Ostindien, skickade ett manuskript till Darwin där han utvecklade en teori om naturligt urval som var väsentligen identisk med Darwins.
                </p>
                <p>
                    1858 publicerade de tillsammans sin evolutionsteori, som ersatte Lamarcks evolutionsteori. Darwin förklarade denna teori i sin bok The Origin of Species (Om arternas uppkomst), där han presenterade sin teori om naturligt urval som evolutionens mekanism.
                </p>
                <ul>
                    Darwins teori om naturligt urval kan uttryckas som fyra påståenden:
                    <li>
                        Inom en given art produceras fler individer genom reproduktion än vad som kan överleva inom de begränsningar (t.ex. matförsörjning) som artens miljö ställer.
                    </li>
                    <li>
                        Följaktligen finns det en <b><i>kamp för tillvaron</i></b>, på grund av skillnaden mellan antalet individer som produceras av reproduktion och antalet som kan överleva.
                    </li>
                    <li>
                        Individer inom en art uppvisar <b><i>variation</i></b>; inga två individer är exakt likadana. De med fördelaktiga karaktärer har större sannolikhet att överleva, och därför att reproducera sig, i kampen för tillvaron.
                    </li>
                    <li>
                        Individer producerar avkomma som tenderar att likna sina föräldrar (principen om <b><i>arv</i></b>). Förutsatt att de fördelaktiga karaktärerna som främjar överlevnad ärvs av avkommor, kommer individer som har dessa karaktärer att bli vanligare i populationen under successiva generationer eftersom de har större chans än individer som inte har dessa karaktärer att överleva och producera avkomma i nästa generation.
                    </li>
                </ul>

                <p>
                    Kärnan i Darwins teori är att naturligt urval kommer att ske om tre förutsättningar är uppfyllda: <b><i>en kamp för tillvaron</i></b>, <b><i>variation</i></b> och <b><i>arv</i></b>.
                </p>
                <h4>
                    Några förtydliganden om denna teori:
                </h4>
                <ul>
                    <li>
                        Enligt Darwin är evolutionen gradvis, långsam och kontinuerlig: det finns inga plötsliga eller bryska förändringar. Det är en gradvis ansamling av små förändringar.
                    </li>
                    <li>
                        Det är miljöfaktorer som driver det naturliga urvalet. Med tiden skulle de individer med egenskaper som är mindre passande för sin miljö försvinna, och de med gynnsamma variationer skulle bevaras. (Den starkaste överlever).
                    </li>
                    <li>
                        Under långa tidsperioder, och alltid på grund av miljöfaktorer, kunde en grupp individer ackumulera en avsevärd mängd gynnsamma egenskaper som ger upphov till en ny art från den ursprungliga populationen.
                    </li>
                    <li>
                        Fortfarande nuförtiden tror många felaktigt att det är darwinismen som förklarar evolutionen. Det finns många invändningar mot den darwinistiska uppfattningen (många av dessa invändningar framfördes medan han fortfarande levde).
                    </li>
                    <li>
                        När Darwin var tvungen att förklara vad som orsakade individuella variationer bland organismer i en population var han tvungen att låna Lamarcks förvärvade karaktäristikteori (Weissman kommer inte att utföra sina experiment förrän 1880). Mendel tillkännagav ärftlighetslagarna sex år efter att Darwin publicerade sin bok, men Mendels arbete erkändes eller uppskattades inte förrän 30 år senare, vilket avsevärt saktade ner förståelsen av evolutionära mekanismer.
                    </li>
                </ul>

                <h3>
                    Den moderna evolutionära syntesen eller neodarwinismen
                </h3>
                <p>
                    En omfattande evolutionsteori som blev känd som den moderna syntesen eller neodarwinismen smiddes i början av 1940-talet. Denna teori är inte ett verk av en utan många vetenskapsmän. Den kallas syntesen eftersom den integrerade upptäckter och idéer från många olika områden, inklusive populationsgenetik, paleontologi, taxonomi, etc. Den sammanför Charles Darwins teori om arternas evolution genom naturligt urval med Gregor Mendels teori om genetik som grund för biologiskt arv.
                </p>
                <p>
                    <b><i>Vi kan definiera evolution som det naturliga urvalet</i></b> (som ses genom differentiell reproduktion), <b><i>som verkar på genetiska variationer</i></b> (som är resultatet av mutationer eller sexuell rekombination) <b><i>som uppträder bland medlemmarna i en population.</i></b>
                </p>
                <ul>
                    Några viktiga aspekter av denna definition är:
                    <li>
                        En population är en lokaliserad grupp av individer som tillhör samma art, där individerna korsar sig sinsemellan och ibland med medlemmar av andra populationer.
                    </li>
                    <li>
                        Som ett resultat av denna slutna sexuella interaktion i en population skapas ett genflöde (det vill säga att de olika individernas gener cirkulerar inom populationen och kan överföras till hela populationen).
                    </li>
                    <li>
                        De genetiska variationerna mellan individer kan bero på:
                        <ul>
                            <li>
                                <b>mutationer</b> (dessa variationer kan ske hos individer med båda typerna av reproduktion, asexuell och sexuell); och
                            </li>
                            <li>
                                <b>meiotisk eller sexuell rekombination</b> (detta äger rum under meiosen - celldelningsprocessen som ger nya könsceller, därför kan det bara hända hos individer med sexuell reproduktion).
                            </li>
                        </ul>
                    </li>
                    <li>
                        På grund av mutationer och meiotiska rekombinationer föds ständigt individer med nya egenskaper. Om dessa individer överlever och får avkomma, kommer deras nya egenskaper att stanna inom populationens genpool eller grupp av gener. I på varandra följande generationer kan dessa genetiska variationer överföras till många (eller alla) medlemmar av populationen.
                    </li>
                    <li>
                        Om det skulle hända eller inte beror på naturligt urval (vissa individer har fler avkommor än andra). De organismer som är bäst anpassade till en given miljö kommer med största sannolikhet att överleva till reproduktiv ålder och få sina egna avkommor. Organismer som är framgångsrika i sina miljöer kommer att vara mer benägna att vara framgångsrika i reproduktion, och därför kommer de bättre anpassade organismerna att föröka sig i högre takt än de mindre välanpassade organismerna. Därför kommer generna hos de som reproducerar sig mer att dominera i populationens genpool. Detta är känt som differentiell reproduktion.
                    </li>
                    <li>
                        Det är viktigt att förstå att det inte bara är hur väl anpassade de är, utan hur väl de klarar av att återge, för att kunna föra vidare de nya karaktärsdragen. Det kan till exempel vara så att en individ är väldigt väl anpassad till sin omgivning, men den har en mutation som gör den steril. Skulle denna individ ha någon inverkan på utvecklingen av sin befolkning?
                    </li>
                    <li>
                        Evolutionsprocessen har därför två steg:
                        <ul>
                            <li>
                                genetisk variation måste uppstå på grund av mutationer och/eller meiotiska rekombinationer;
                            </li>
                            <li>
                                dessa variationer måste spridas över hela befolkningen genom differentiell reproduktion (naturligt urval) i successiva generationer.
                            </li>
                        </ul>
                    </li>
                    <li>
                        Individer utvecklas inte genetiskt, det gör populationer. De genetiska egenskaperna hos en individ ändras vanligtvis inte under hela livet och när den väl dör försvinner de. Men om individen reproducerar sig sexuellt kan den introducera genetiska variationer till sin avkomma.
                    </li>
                    <li>
                        Evolutionsprocessen följer inte ett fast mönster, eftersom genetiska variationer sker slumpmässigt. Detta betyder inte att evolutionen saknar en drivkraft: eftersom de individer som är mest framgångsrika i reproduktion är de som är bäst anpassade, är evolutionens drivkraft naturligt urval. – Därför verkar naturliga urval med hjälp av differentiell reproduktion, och inte genom kampen för överlevnad.
                    </li>
                </ul>

                <h2>
                    Exempel
                </h2>

                <h3>
                    Evolution av giraffen enligt Lamarck:
                </h3>
                <p>
                    Giraffens förfäder (med kort hals och korta ben) åt också löv. När de nedre löven åts upp, var den tvungen att sträcka halsen till nya längder i jakten på att äta de högre löven. På grund av den "medfödda impulsen" började dess nacke och ben växa. Den långa halsen och benen på giraffen, Lamarck resonerade, utvecklades gradvis som den kumulativa produkten av många generationer av förfäder som sträckte sig högre och högre, som skulle ärvas av avkommor.
                </p>
                <h3>
                    Evolution av giraffen enligt Darwin:
                </h3>
                <p>
                    Variabiliteten som uppträdde i giraffpopulationer i varje generation (en del skulle sträcka på nacken eller benen mer än andra) gav upphov till att vissa individer inom populationen uppträdde med längre ben och en längre hals. Dessa karaktärer överfördes till deras avkomma. Till en början var detta inte till någon fördel för individen, eftersom det fanns tillräckligt med låga löv på träden. Men eftersom de nedre löven blev knappa kunde bara individerna med längre hals och längre ben nå de högre löven, vilket gjorde att de kunde överleva genom generationer och få fler avkommor. Med tiden blev det den enda typen av giraff som fanns.
                </p>
                <h3>
                    Evolution av giraffen enligt den moderna evolutionära syntesen:
                </h3>
                <p>
                    Förfäderna till dagens giraffer hade inte lång hals eller långa framben. Genom mutation och/eller genetisk rekombination uppträdde nya individer med lång hals och/eller långa ben i en population. De nya individerna var bättre anpassade till miljön. De åt mer, hittade lättare parningspartner och förökade sig därför oftare. Med tiden blev de de enda existerande girafferna. Uppenbarligen, de individer som uppvisade kort hals och korta ben (båda negativa egenskaper), eftersom de var mindre väl anpassade till miljön, reproducerade sig inte lika framgångsrikt som de välanpassade girafferna. Och så småningom försvann de mindre gynnsamma generna från befolkningen.
                </p>
            </section>
        
