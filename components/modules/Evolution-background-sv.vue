                <h2 id="s3t">
                    Evolution: myter och missuppfattningar
                </h2>

                <h3>
                    <span class="text-rose-400">
                        Myt 1:
                    </span>
                    Evolution producerar perfekt anpassade varelser
                </h3>
                <p>
                    Naturligt urval är inte allsmäktigt. Det finns många anledningar till att naturligt urval inte kan producera "perfekt konstruerade" egenskaper. Till exempel, levande varelser består av egenskaper som är ett resultat av en komplicerad uppsättning avvägningar – att ändra en egenskap till det bättre kan innebära att en annan förändras till det sämre (t.ex. en fågel med den "perfekta" stjärtfjäderdräkten för att attrahera en partner kanske blir särskilt sårbar för rovdjur). Och naturligtvis, eftersom organismer har uppstått genom komplexa evolutionära historier (inte en designprocess), är deras framtida evolution ofta begränsad av egenskaper som de redan har utvecklat. Till exempel, även om det vore fördelaktigt för en insekt att växa på något annat sätt än genom att röta, så kunde denna växling helt enkelt inte ske eftersom rötning är inbäddad i insekters genetiska sammansättning på många nivåer. När allt kommer omkring behöver du inte vara perfekt anpassad för att överleva, du måste bara vara lika väl anpassad som dina konkurrenter.
                </p>
                <p>
                    Det finns många anledningar till varför naturligt urval kanske inte ger en "perfekt konstruerad" egenskap. Till exempel kan du föreställa dig att geparder kunde fånga fler byten och få fler avkommor om de kunde springa bara lite snabbare. Till exempel, här är några anledningar till varför naturligt urval kanske inte ger perfektion eller snabbare geparder:
                </p>
                <ul>
                    <li>
                        <b>
                            Brist på nödvändig genetisk variation
                        </b>
                        <br />
                        Selektion kan endast verka på den tillgängliga genetiska variationen. En gepard kan springa snabbare om den hade "snabbare" alleler - men om snabbare alleler inte finns i populationen från mutation eller genflöde, kommer evolution i denna riktning inte att ske.
                    </li>
                    <li>
                        <b>
                            Begränsningar på grund av historien
                        </b>
                        <br />
                        Kanske skulle ett annat arrangemang av benmuskler och ben producera geparder som springer snabbare - dock är den grundläggande kroppsformen för däggdjur redan utlagd i deras gener och utveckling på ett så ömsesidigt begränsat sätt att det är osannolikt att det kommer att förändras. Det kanske verkligen inte finns något sätt att ta sig dit härifrån.
                    </li>
                    <li>
                        <b>
                            Avvägningar
                        </b>
                        <br />
                        Att ändra en funktion till det bättre kan förändra en annan till det sämre. Kanske finns det snabbare alleler i gepardpopulationen - men det finns en avvägning förknippad med dem: allelerna producerar geparder med längre ben (och därmed snabbare), men dessa långa ben är farligt känsliga. Även om längre lemben ökar stegen, ökar deras chanser att misslyckas på grund av böjningsbelastningar också. I det här fallet kanske ingen nettoökning i kondition skulle bli resultatet av de snabbare allelerna.
                    </li>
                </ul>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Myt 2:
                    </span>
                    Evolution är bara en teori.
                </h3>
                <p>
                    Inom vetenskapen är en teori en förklaring till ett fenomen som kan testas och verifieras med den vetenskapliga metoden. Den innehåller flera beviskällor som byggs upp i ett ramverk av förståelse som kan förändras över tiden för att ta hänsyn till nya upptäckter och nya bevis.
                </p>
                <p>
                    Teorier är de förklaringar som vetenskapen söker hitta. I dagligt bruk är ordet något mindre konkret.
                </p>
                <p>
                    Missuppfattningen "Evolution är bara en teori" härrör från en sammanblandning mellan vardagligt och vetenskaplig användning av ordet teori. I vardagsspråket används teori ofta för att betyda en idé med litet bevisstöd. "Vetenskaplig teori" å andra sidan innebär inte mycket osäkerhet. Det är en sammanhängande grupp allmänna påståenden som används som förklaringsprinciper för en klass av fenomen. För att bli accepterad av det vetenskapliga samfundet måste en teori ha starkt stöd av många olika bevis. När det gäller evolutionsteorin kan nämnas följande bevisen:
                </p>
                <ul>
                    <li>
                        Livet dök upp på jorden för mer än två miljarder år sedan;
                    </li>
                    <li>
                        Livsformer har förändrats och diversifierats under livets historia;
                    </li>
                    <li>
                        Arter är besläktade via gemensam härkomst från en eller några få gemensamma förfäder;
                    </li>
                    <li>
                        Naturligt urval är en viktig faktor som påverkar hur arter förändras.
                    </li>
                </ul>
                <p>
                    Evolutionsteorin har har användbara tillämpningar inom epidemiologi, skadedjursbekämpning, läkemedelsupptäckt och andra områden.
                </p>
                <p>
                    Förutom teorin finns det evolutionens faktum, observationen att livet har förändrats mycket över tiden. Faktumet om evolution var erkänt redan före Darwins teori. Evolutionsteorin förklarar faktumet.
                </p>
                <p>
                    Med uttrycket "bara en teori" borde någon också förkasta gravitationsteori, atomteorin, bakterieteorin om sjukdomar etc. Evolutionsteorin är inte mindre giltig än någon av dessa. Även gravitationsteorin får fortfarande allvarliga utmaningar. Ändå är gravitationsfenomenet, liksom evolutionen, fortfarande ett faktum.
                </p>
                <p>
                    Så, evolution är en väl underbyggd och brett accepterad vetenskaplig teori; det är inte "bara" en idé.
                </p>


                <hr />
                <h3>
                    <span class="text-rose-400">
                        Myt 3:
                    </span> 
                    Evolution följer en förutbestämd, rak väg (eller om människan utvecklats från apor, varför finns det fortfarande apor?)
                </h3>
                <p>
                    En annan missuppfattning är att evolution är en strikt linjär process - det vill säga att den sker i en rak linje från primitiv till avancerad.
                </p>
                <UiContentImage src="asdads" :elevated="true">
                    
                </UiContentImage>
                <p>
                    Med ursprung i Platon och Aristoteles var den traditionella synen på hur världen var organiserad genom en "progression i perfektion". Detta koncept är explicit i idén om "scala naturae": Alla varelser på jorden, levande och livlösa, skulle kunna organiseras enligt en ökande skala av perfektion från svampar längst ner och upp genom hummer och kaniner, hela vägen till människor i toppen. Denna uppfattning innehåller tre huvudsakliga fel:
                </p>
                <ul>
                    <li>
                        Den hävdar att naturen är organiserad hierarkiskt. Det är inte ett slumpmässigt urval av varelser.
                    </li>
                    <li>
                        Den utgår från två organiserings principer: saker går från enkelt till perfekt och från primitivt till modernt.
                    </li>
                    <li>
                        Den antar att det inte finns några mellanliggande stadier mellan nivåerna i denna hierarki. Varje nivå är ett vattentätt fack av liknande komplexitet. Ingen är halvvägs mellan två steg.
                    </li>
                </ul>
                <p>
                    Sanningen är att vi inte har utvecklats från något av de djur som lever idag. Människor har inte utvecklats från gorillorna eller schimpanserna vi ser i djurparken och är en vanlig missuppfattning att apor är ett steg bort från att bli människor. Enligt Darwin är alla nuvarande organismer lika utvecklade och alla påverkas fortfarande av naturligt urval. Så, en sjöstjärna och en person, till exempel, ligger båda i framkant av utvecklingen av sina specifika uppbyggnadsplaner. Och de råkar ha en gemensam förfader som levde för cirka 580 miljoner år sedan. Det betyder att människor härstammar från förfäder (som är nu utdöda) som levde för miljoner år sedan. Summan av kardemumman är att människor har apförfäder och som sådana är alla människor släkt med andra apor.
                </p>
                <p>
                    Darwins teori förutsätter ingen speciell riktning i evolutionen. Det förutsätter gradvis förändring och diversifiering. Och eftersom evolutionen fortfarande fungerar idag, är alla närvarande organismer de mest utvecklade av sitt slag.
                </p>
                <p>
                    Evolution är inte en linjär process som börjar med mer "primitivt" utseende organismer vi kan observera idag, och slutar i mänskligheten (som visas i figuren ovan). Ett kladogram fångar den viktigaste aspekten av den evolutionära processen: "artförgrening", eller vad biologer kallar kladogenesis. Kladogenetiska händelser är de ögonblick i tiden då en art "delas" i två arter - dessa händelser är också kända som artbildningshändelser. Kladogenesens förgreningskaraktär har en viktig konsekvens: Eftersom två eller flera nya arter alltid härstammar från en förfaderart (och denna process har pågått sedan livets ursprung), är alla två arter som vi observerar i nuet släkt.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Myt 4:
                    </span>
                    Evolution är ovetenskapligt eftersom det inte är testbart eller falsifierbart. Den gör påståenden om händelser som inte observerades och som aldrig kan återskapas.
                </h3>
                <p>
                    Evolutionsvetenskapen är uppdelad i minst två områden: mikroevolution och makroevolution.
                </p>
                <ul>
                    <li>
                        <b>Mikroevolution</b> tittar på förändringar inom arter över tid som kan vara förspel till artbildning, ursprunget till nya arter.
                    </li>
                    <li>
                        <b>Makroevolution</b> studerar hur taxonomiska grupper över artnivån förändras. Dess bevis hämtar ofta från fossilregistret och DNA-jämförelser för att rekonstruera hur olika organismer kan vara släkt.
                    </li>
                </ul>
                <p>
                    Nuförtiden erkänner även de flesta kreationister att mikroevolutionen har upprätthållits av tester i laboratoriet (som i studier av celler, växter och fruktflugor) och i fält (som i Grants studier av utvecklande näbbformer bland Galpagosfinkar). Naturligt urval och andra mekanismer (kromosomförändringar, symbios och hybridisering) kan driva på djupgående förändringar i populationer över tid.
                </p>
                <p>
                    Makroevolutionär studie involverar slutsatser från fossiler och DNA snarare än direkt observation. Men inom de historiska vetenskaperna (som inkluderar astronomi, geologi och arkeologi, såväl som evolutionsbiologi), kan hypoteser fortfarande testas genom att kontrollera om de överensstämmer med fysiska bevis och om de leder till verifierbara förutsägelser om framtida upptäckter. Till exempel innebär evolutionen att man mellan de tidigaste kända förfäderna till människor (ungefär fem miljoner år gamla) och utseendet på anatomiskt moderna människor (för ungefär 200 000 år sedan), bör hitta en följd av homininvarelser med drag som gradvis blir mindre aplika och mer moderna , vilket verkligen är vad fossilregistret visar. Men man bör inte (och inte) hitta moderna mänskliga fossiler inbäddade i skikt från juraperioden (65 miljoner år sedan). Evolutionsbiologi gör rutinmässigt förutsägelser mycket mer raffinerade och exakta än så här, och forskare testar dem ständigt.
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Myt 5:
                    </span>
                    Arter är oföränderliga.
                </h3>
                <p>
                    Uppfattningen att levande arter av djur och växter är oföränderliga är förmodligen lika gammal som mänskligheten. En tillfällig observation av den naturliga världen tyder inte lätt på att arter utvecklas. Detta beror på att mänskliga livslängder är för korta för att bevittna dessa händelser direkt. Speciation kan ta århundraden. Dessutom kan det vara svårt att känna igen en ny art under ett formationsskede eftersom biologer ibland är oense om hur man bäst definierar en art.
                </p>
                <p>
                    Ändå innehåller den vetenskapliga litteraturen rapporter om uppenbara artbildningshändelser hos växter, insekter och maskar. I de flesta av dessa experiment utsatte forskare organismer för olika typer av urval – för anatomiska skillnader, parningsbeteenden, habitatpreferenser och andra egenskaper – och fann att de hade skapat populationer av organismer som inte häckade med utomstående.
                </p>
                <p>
                    Även om vi inte kan köra ett experiment som kommer att berätta för oss hur dinosaurielinjen strålade, kan vi studera många aspekter av evolutionen med kontrollerade experiment i laboratoriemiljö. I organismer med korta generationstider (t.ex. bakterier eller fruktflugor) kan vi faktiskt observera evolution i aktion under loppet av ett experiment. Utvecklingen av bakterier på en <a href="https://www.youtube.com/watch?v=plVk4NVIUh8" target="_blank" rel="noopener noreferrer">Utvecklingen av bakterier på en "mega-platta" petriskål</a>. I ett annat exempel visade William R. Rice från University of New Mexico och George W. Salt från University of California, Davis, att om de sorterade en grupp fruktflugor efter deras preferens för vissa miljöer och födde upp dessa flugor separat över 35 generationer, skulle de resulterande flugorna vägra att häcka med dem från en helt annan miljö. I vissa fall har biologer observerat evolution som sker i naturen (t.ex. myggpopulationer som utvecklar snabb resistens mot DDT, antibiotikaresistenta bakterier och läkemedelsresistent HIV kan utveckla resistens mot läkemedel mycket snabbt).
                </p>

                <hr />
                <h3>
                    <span class="text-rose-400">
                        Myt 6:
                    </span>
                    Användning eller obruk av ett organ under ens livstid, kan ärvas av dess avkomma och leda till en förändring i en art.
                </h3>

                <p>
                    Lamarck föreslog en evolutionsteori baserad på principen att fysiska förändringar i organismer under deras livstid – såsom större utveckling av ett organ eller en del genom ökad användning – skulle kunna överföras till deras avkomma.
                </p>
                <p>
                    Biologer definierar en förvärvad egenskap som en egenskap som har utvecklats under en individs liv i de somatiska eller kroppscellerna, vanligtvis som ett direkt svar på någon yttre förändring i miljön eller genom att en del används eller inte används. Nedärvningen av en sådan egenskap innebär att den återkommer hos en eller flera individer i nästa eller i efterföljande generationer. men det finns inga bevis som stöder detta fall.
                </p>
                <p>
                    Förutom att växa kan enskilda organismer utveckla speciella färdigheter eller fysiska karaktärer under livets gång som ett resultat av skillnader i hur de lever. Till exempel mänskliga metoder för öronhåltagning, omskärelse och dekorativa kroppsärr. Dessa karaktärer, som förvärvas medvetet under loppet av en individs liv, ärvs inte av den individens avkomma även om praktiken kan ha utförts i hundratals generationer. Likaså kommer en växt som har växt sig särskilt stor i en yta med bra mark, eller en padda som har växt sig mycket stor för att den lever i en trädgård full av mat, inte överföra sin stora storlek till sin avkomma. Så, arv av förvärvade karaktärer förekommer inte.
                </p>
                <p>
                    Ett annat exempel skulle kunna finnas i det förmodade arvet av en förändring som åstadkoms genom användning och ur bruk av ett speciellt organ. Smedens arm (eller någon annan uppsättning muskler) förstoras när den används kontinuerligt mot ett yttre motstånd, såsom hammarens vikt. Om effekten gick i arv skulle smedens barn vid födseln ha ovanligt stora armar — om inte vid födseln, så när de blev vuxna, trots att de inte hade använt armarna överdrivet. Det finns inga bevis som stöder detta fall.
                </p>
                <p>
                    En mer subtil illustration finns i det förmodade arvet av en ökad fingerfärdighet hos en musikers händer genom övning. Den färdighet som förvärvats, även om den inte orsakar någon synlig ökning av storleken på fingrarna, kan tänkas föras vidare till musikerns barn, och de kan då förväntas spela skickligt med minimal träning. Hur det komplicerade samspelet mellan hjärnsekvenser som har gett musikerns fingrar skicklighet någonsin kunde överföras till musikerns könsceller (spermatozoer eller ägg), och genom dem till eventuella potentiella barn, har aldrig förts inom ramen för biologiska möjligheter .
                </p>
            </section>





