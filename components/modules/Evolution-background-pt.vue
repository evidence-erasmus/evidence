                <h2 id="s2t">
                    Teorias evolução
                </h2>
                <h3>
                    Teoria da evolução de Lamarck
                </h3>
                <ol>
                    A teoria de Lamarck baseia-se em:
                    <li>
                        A evolução foi impulsionada por uma tendência inata para uma complexidade cada vez maior, para a perfeição. (força vital).
                    </li>
                    <li>
                        Esta tendência inata para a perfeição é influenciada pelas alterações ambientais, tornando-se assim mais bem adaptada aos seus ambientes. Estas alterações ambientais criariam "novas necessidades" nos organismos, que precisariam de utilizar determinados órgãos ou características mais do que outros.
                    </li>
                    <li>
                        Esta utilização e não utilização de certos órgãos faz com que os órgãos que são utilizados mais frequentemente se adaptem melhor ao ambiente, tornando-se maiores e mais fortes, enquanto os que não são utilizados se deterioram. Por outras palavras: a função faz o órgão. Assim, os organismos podem adquirir certas características ao longo da sua vida e perder outras.
                    </li>
                    <li>
                        Lamarck acreditava que as modificações que um organismo adquire durante a sua vida são transmitidas aos seus descendentes (herança das características adquiridas).
                    </li>
                    <li>
                        Uma espécie não se pode transformar em qualquer outra espécie, mas apenas numa espécie semelhante, onde a espécie pode evoluir gradualmente. Lamarck estabeleceu o facto da evolução, mas a sua teoria tinha várias falhas:
                        <ul>
                            <li>
                                Não há provas que comprovem a tendência inata (impulso vital) dos organismos para a complexidade.
                            </li>
                            <li>
                                As características adquiridas não são herdadas. Todos sabemos que o exercício físico faz com que os nossos músculos se desenvolvam, mas essas características físicas adquiridas (que não são genéticas) não são transmitidas aos descendentes. Estas características adquiridas são produzidas por fatores ambientais e pelo desenvolvimento, mas não pelos genes. Apenas os caracteres que são regulados por genes são hereditários, e apenas se esses genes se encontrarem nas células reprodutoras. O que quer que aconteça às células somáticas devido ao seu uso ou desuso não afetará os genes nos gâmetas, logo não será hereditário. F.L. August Weissmann (1834-1914) realizou a seguinte experiência para refutar a teoria de Lamarck: cortou as caudas de mil e quinhentos ratos, repetidamente ao longo de 20 gerações, e verificou que nenhum rato nasceu sem cauda. De acordo com Lamarck e a sua teoria, o desuso da cauda deveria ter dado origem a descendentes sem cauda ou com uma cauda pouco desenvolvida, mas nunca foi esse o caso.
                            </li>
                        </ul>
                    </li>
                </ol>

                <h3>
                    A teoria da evolução de Darwin-Wallace
                </h3>
                <p>
                    Charles Darwin tinha 22 anos quando partiu de Inglaterra com o Beagle em dezembro de 1831; uma viagem à volta do mundo que duraria cinco anos. Durante a expedição, Darwin recolheu milhares de espécimes das faunas e floras exóticas e extremamente diversificadas de diferentes lugares. Também pôde observar as várias adaptações de plantas e animais que habitavam ambientes tão diversos.
                </p>
                <ul>
                    Embora Darwin não soubesse nada sobre o mecanismo da herança, ele estava muito consciente de muitos outros aspetos dos organismos vivos. Entre eles, três são particularmente enfatizados:
                    <li>
                        As espécies que habitam a Terra hoje não são as mesmas que existiram no passado, embora se assemelhem a elas. Este aspeto da evolução era muito evidente para Darwin a partir do registo fóssil.
                    </li>
                    <li>
                        Cada espécie possui uma série de caracteres que adaptam os indivíduos dessa espécie ao seu modo de vida e ao seu ambiente particular. Grande parte de A Origem das Espécies é dedicada a descrições detalhadas das adaptações de espécies individuais, por exemplo, as várias formas de bico dos tentilhões nas Ilhas Galápagos.
                    </li>
                    <li>
                        A reprodução seletiva de espécies domésticas pode produzir caracteres numa diversidade de formas. Por exemplo, os criadores de cães produziram numerosas raças que diferem em caracteres como o comprimento das orelhas, a estatura e o comportamento: raças diferentes têm formas diferentes de um carácter.
                    </li>
                </ul>
                <p>
                    Outro naturalista, Alfred Wallace, que trabalhava nas Índias Orientais, enviou um manuscrito a Darwin no qual desenvolvia uma teoria da seleção natural essencialmente idêntica à de Darwin. 
                </p>
                <p>
                    Em 1858, publicaram em conjunto a sua teoria da evolução, que substituiu a teoria evolutiva de Lamarck. Darwin explicou esta teoria no seu livro A Origem das Espécies, onde apresentou a sua teoria da seleção natural como o mecanismo da evolução.
                </p>
                <ul>
                    A teoria da seleção natural de Darwin pode ser expressa em quatro proposições:
                    <li>
                        Dentro de uma determinada espécie, são produzidos mais indivíduos por reprodução do que aqueles que podem sobreviver dentro das restrições (por exemplo, oferta de alimentos) impostas pelo ambiente da espécie.
                    </li>
                    <li>
                        Consequentemente, há uma luta pela existência, devido à disparidade entre o número de indivíduos produzidos pela reprodução e o número que pode sobreviver.
                    </li>
                    <li>
                        Os indivíduos de uma espécie apresentam <b>variações</b>; não há dois indivíduos exatamente iguais. Os indivíduos com caracteres vantajosos têm uma maior probabilidade de sobreviver e, por conseguinte, de se reproduzir, na luta pela existência.
                    </li>
                    <li>
                        Os indivíduos produzem descendentes que tendem a assemelhar-se aos seus progenitores (princípio da <b>hereditariedade</b>). Desde que os caracteres vantajosos que promovem a sobrevivência sejam herdados pelos descendentes, os indivíduos que possuem esses caracteres tornar-se-ão mais comuns na população ao longo das sucessivas gerações, porque têm mais probabilidades do que os indivíduos que não possuem esses caracteres de sobreviver e produzir descendentes na geração seguinte.
                    </li>
                </ul>

                <p>
                    A essência da teoria de Darwin é que a seleção natural ocorrerá se estiverem reunidas três condições. Estas condições são a luta pela existência, a variação e a hereditariedade.
                </p>
                <p>
                    <b>Alguns esclarecimentos sobre esta teoria:</b>
                </p>
                <ul>
                    <li>
                        Segundo Darwin, a evolução é gradual, lenta e contínua: não há mudanças súbitas ou bruscas. Trata-se de uma acumulação gradual de pequenas alterações.
                    </li>
                    <li>
                        Os fatores ambientais são o motor da seleção natural. Com o tempo, os indivíduos com características menos adaptadas ao seu ambiente desapareceriam, e aqueles com variações favoráveis perpetuar-se-iam. (Sobrevivência do mais apto).
                    </li>
                    <li>
                        Ao longo de vastos períodos de tempo, e sempre devido a fatores ambientais, um grupo de indivíduos pode acumular uma quantidade considerável de características favoráveis, dando origem a uma nova espécie a partir da população original.
                    </li>
                    <li>
                        Ainda hoje, muitas pessoas acreditam erradamente que é o darwinismo que explica a evolução. Há muitas objeções à visão darwinista (muitas dessas objeções foram apresentadas quando ele ainda era vivo).
                    </li>
                    <li>
                        Quando Darwin teve de explicar a causa das variações individuais entre organismos de uma população, teve de recorrer à teoria das características adquiridas de Lamarck (Weissman só realizará as suas experiências em 1880). Mendel enunciou as leis da hereditariedade seis anos depois de Darwin ter publicado o seu livro, mas o trabalho de Mendel só foi reconhecido ou apreciado 30 anos mais tarde, o que atrasou consideravelmente a compreensão dos mecanismos evolutivos.
                    </li>
                </ul>

                <h3>
                    A síntese evolutiva moderna ou neodarwinismo
                </h3>
                <p>
                    Uma teoria abrangente da evolução que ficou conhecida como a síntese moderna ou neodarwinismo foi forjada no início da década de 1940. Esta teoria não é o trabalho de um, mas de muitos cientistas. É chamada de síntese porque integrou descobertas e ideias de muitos campos diferentes, incluindo genética populacional, paleontologia, taxonomia, etc. Reúne a teoria de Charles Darwin da evolução das espécies por seleção natural com a teoria da genética de Gregor Mendel como base da herança biológica.
                </p>
                <p>
                    <b>Podemos definir a evolução como a seleção natural</b> (que se manifesta através da reprodução diferencial), <b>que atua sobre as variações genéticas</b> (que são o resultado de mutações ou da recombinação sexual) <b>que surgem entre os membros de uma população.</b>
                </p>
                <ul>
                    Alguns aspetos importantes desta definição são:
                    <li>
                        Uma população é um grupo localizado de indivíduos pertencentes à mesma espécie, onde os indivíduos se cruzam entre si e, ocasionalmente, com membros de outras populações.
                    </li>
                    <li>
                        Como resultado desta interação sexual fechada numa população, cria-se um fluxo genético (ou seja, os genes dos diferentes indivíduos circulam dentro da população e podem ser transmitidos a toda a população).
                    </li>
                    <li>
                        As variações genéticas entre indivíduos podem dever-se a: 
                        <ul>
                            <li>
                                As mutações (estas variações podem ocorrer em indivíduos com ambos os tipos de reprodução, assexuada e sexuada); e 
                            </li>
                            <li>
                                a recombinação meiótica ou sexual (ocorre durante a meiose, portanto, só pode ocorrer em indivíduos com reprodução sexuada).
                            </li>
                        </ul>
                    </li>
                    <li>
                        Devido às mutações e às recombinações meióticas, nascem constantemente indivíduos com novas características. Se esses indivíduos sobreviverem e tiverem descendência, as suas novas características manter-se-ão no património genético ou no grupo de genes da população. Em gerações sucessivas, essas variações genéticas podem ser transmitidas a muitos (ou a todos) os membros da população.
                    </li>
                    <li>
                        Se isso aconteceria ou não, depende da seleção natural (alguns indivíduos têm mais descendentes do que outros). Os organismos mais bem adaptados a um determinado ambiente terão mais probabilidades de sobreviver até à idade reprodutiva e de ter descendência própria. Os organismos que são bem sucedidos nos seus ambientes terão mais probabilidades de serem bem sucedidos na reprodução e, por conseguinte, os organismos mais bem adaptados reproduzir-se-ão a uma taxa maior do que os organismos menos bem adaptados. Por conseguinte, os genes dos organismos que se reproduzem mais, irão predominar no património genético da população. Este fenómeno é conhecido como reprodução diferencial.
                    </li>
                    <li>
                        É importante compreender que não se trata apenas de saber quão bem adaptados estão, mas também quão bem são capazes de se reproduzir, para poderem transmitir os novos caracteres. Por exemplo, pode acontecer que um indivíduo esteja muito bem adaptado ao seu ambiente, mas tenha uma mutação que o torna estéril. Teria este indivíduo algum impacto na evolução da sua população?
                    </li>
                    <li>
                        O processo de evolução tem, portanto, duas fases: 
                        <ul>
                            <li>
                                a variação genética deve ocorrer devido a mutações e/ou recombinações meióticas; 
                            </li>
                            <li>
                                essas variações devem espalhar-se pela população através da reprodução diferencial (seleção natural) em gerações sucessivas.
                            </li>
                        </ul>
                    </li>
                    <li>
                        Os indivíduos não evoluem, as populações sim. As características genéticas que um indivíduo possui normalmente não variam ao longo da sua vida e, quando morre, desaparecem. No entanto, se o indivíduo se reproduzir sexualmente, pode introduzir variações genéticas na sua descendência.
                    </li>
                    <li>
                        processo de evolução não segue um padrão fixo, uma vez que as variações genéticas ocorrem de forma aleatória. Isto não significa que a evolução não tenha uma força motriz: como os indivíduos que têm mais sucesso na reprodução são os que estão mais bem adaptados, a força motriz da evolução é a seleção natural. - Assim, a seleção natural atua através da reprodução diferencial e não através da luta pela sobrevivência.
                    </li>
                </ul>

                <h2>
                    Exemplos
                </h2>

                <h3>
                    Evolução da girafa segundo Lamarck:
                </h3>
                <p>
                    Os antepassados da girafa (com pescoço e pernas curtos) também comiam folhas. À medida que as folhas mais baixas eram comidas, a girafa tinha de esticar o pescoço até um novo comprimento para poder comer as folhas mais altas. Devido a esse "impulso inato", o seu pescoço e as suas pernas começaram a crescer. O pescoço e as pernas compridos da girafa, segundo Lamarck, evoluíram gradualmente como produto cumulativo de muitas gerações de antepassados que se esticavam cada vez mais alto e que seriam herdados pela descendência.
                </p>
                <h3>
                    Evolução da girafa segundo Darwin:
                </h3>
                <p>
                    A variabilidade que surgia nas populações de girafas em cada geração (algumas esticavam o pescoço ou as pernas mais do que outras) deu origem ao aparecimento de alguns indivíduos dentro da população com pernas e pescoço mais compridos. Estes caracteres foram transmitidos aos seus descendentes. No início, isto não era vantajoso para o indivíduo, pois havia suficientes folhas baixas nas árvores. No entanto, à medida que as folhas baixas foram escasseando, apenas os indivíduos com o pescoço e as pernas mais compridos conseguiam alcançar as folhas mais altas, o que lhes permitia sobreviver ao longo das gerações e ter mais descendentes. Com o tempo, tornou-se o único tipo de girafa que existia.
                </p>
                <h3>
                    Evolução da girafa de acordo com a síntese evolutiva moderna: 
                </h3>
                <p>
                    Os antepassados das girafas atuais não tinham um pescoço comprido nem patas dianteiras compridas. Por mutação e/ou recombinação genética, surgiram numa população novos indivíduos que apresentavam um pescoço longo e/ou pernas longas. Esses novos indivíduos estavam mais bem adaptados ao ambiente. Comiam mais, encontravam parceiros de acasalamento mais facilmente e, por conseguinte, reproduziam-se com mais frequência. Com o tempo, tornaram-se as únicas girafas existentes. Obviamente, os indivíduos que apresentavam o pescoço curto e as pernas curtas (ambas características negativas), como estavam menos bem adaptados ao ambiente, não se reproduziam com tanto sucesso como as girafas bem adaptadas. E, eventualmente, os genes menos favoráveis desapareceram da população.
                </p>
