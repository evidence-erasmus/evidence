                <p>
                    Um <b>dilema moral</b> apresentado no final do módulo (Atividade 6) deve levar os alunos a refletir sobre as suas escolhas e as dos outros e sobre as consequências dessas escolhas para os diferentes intervenientes envolvidos numa determinada situação. As decisões potenciais e o raciocínio que lhes está subjacente baseiam-se principalmente na teoria do desenvolvimento moral de Kolberg, que inclui o seguinte (os exemplos para ilustrar os níveis são retirados da atividade 6, mas são igualmente possíveis outras interpretações):
                </p>

                <h3>
                    Nível 1 (Pré-Convencional)
                </h3>
                <ol>
                    <li>
                        <b>Orientação para a obediência e a punição.</b> Os indivíduos concentram-se nas consequências directas das suas acções para si próprios. Uma ação é considerada moralmente errada porque o seu autor é punido. Comum nas crianças. As regras são fixas e absolutas. A questão principal é: "como posso evitar o castigo?”
                        <br />
                        <i>Por exemplo, Dani não deve ficar com a droga  porque isso pode resultar num registo criminal para si, o que significa que é uma pessoa má.</i>
                    </li>
                    <li>
                        <b>Orientação para o interesse próprio (instrumental-relativista):</b> Pagar por um benefício. O comportamento correto é definido pelo que o indivíduo considera ser do seu interesse, ou pelo que é "conveniente", mas entendido de uma forma restrita que não tem em conta a sua reputação ou as relações com grupos de pessoas. O raciocínio mostra um interesse limitado nas necessidades dos outros, mas apenas até ao ponto em que isso possa favorecer os interesses do próprio indivíduo.
                        <br />
                        <i>Por exemplo, Dani deve aceitar ficar com a substância porque Marcos já fez vários favores a Dani no passado e assim agora pode retribuir.</i>
                        <br />
                        <i>Dani deve devolvê-la a Marcos porque um amigo criminoso será inútil.</i>
                    </li>
                </ol>
                <h3>
                    Nível 2 (Convencional)
                </h3>
                <ol start="3">
                    <li>
                        <b>Acordo interpessoal e conformidade com as normas sociais.</b> Os indivíduos são receptivos à aprovação ou desaprovação dos outros, uma vez que esta reflecte os pontos de vista da sociedade. Tentam ser um "bom rapaz" ou uma "boa rapariga" para corresponder a essas expectativas, tendo aprendido que ser considerado bom beneficia o eu. O raciocínio da terceira fase pode julgar a moralidade de uma ação avaliando as suas consequências em termos das relações de uma pessoa, que agora começam a incluir coisas como o respeito, a gratidão e a "regra de ouro". Capacidade de reconhecer boas ou más intenções. A atitude de bom rapaz/rapariga.
                        <br />
                        <i>Por exemplo, Dani deve ficar com ela porque não pode desiludir o seu amigo de longa data.</i>
                        <br />
                        <i>Por exemplo, Dani deve devolvê-la para não desiludir os seus pais, caso seja apanhado com drogas pela polícia.</i>
                    </li>
                    <li>
                        <b>Orientação para a manutenção da autoridade e da ordem social:</b> Moral da lei e da ordem, capacidade de ver sistemas normativos abstractos ("Cumpre o teu dever"). É importante obedecer às leis e às convenções sociais devido à sua importância para a manutenção de uma sociedade funcional. O raciocínio moral na quarta fase ultrapassa, assim, a necessidade de aprovação individual que se manifesta na terceira fase. Um ideal ou ideais centrais prescrevem frequentemente o que é certo e errado. Se uma pessoa violar uma lei, talvez todos o façam - assim, há uma obrigação e um dever de respeitar as leis e as regras. Quando alguém viola uma lei, isso é moralmente errado.
                        <br />
                        <i>Por exemplo, Dani deve devolvê-la a Marcos porque ter e consumir erva é proibido.</i>
                    </li>
                </ol>
                <h3>
                    Nível 3 (Pós-Convencional)
                </h3>
                <ol start="5">
                    <li>
                        <b>Orientação para o contrato social</b> - as diferentes opiniões, direitos e valores devem ser mutuamente respeitados como sendo exclusivos de cada pessoa ou comunidade. As leis são encaradas como <a href="https://en.wikipedia.org/wiki/Social_contract" target="_blank" rel="noopener noreferrer">contratos sociais</a> e não como decretos rígidos. Os direitos pessoais são importantes. Isto é conseguido através de uma decisão maioritária e de um compromisso inevitável. O governo democrático é supostamente baseado no raciocínio da quinta fase.
                        <br />
                        <i>Por exemplo, Dani deve ficar com ela para evitar que o seu amigo seja condenado a uma pena criminal, o que o pode levar ainda mais longe na sua queda pessoal.</i>
                        <br />
                        <i>Por exemplo, Dani deve devolvê-la, pois tentar evitar que o Marcos seja condenado por uma pequena quantidade de estupefacientes pode levá-lo a um caso mais significativo relacionado com estupefacientes. Poderia assim receber uma lição de vida para evitar o tráfico de drogas.</i>
                        <br />
                        <i>Por exemplo, Dani não deve ficar com ela, pois isso pode resultar num registo criminal que impeça a entrada na academia militar.</i>
                        <br />
                        <i>Por exemplo, Dani deve devolvê-la, pois não seria justo ter um castigo por um ato que não cometeu.</i>
                    </li>
                    <li>
                        <b>Princípios éticos universais</b> - o raciocínio moral baseia-se num <a href="https://en.wikipedia.org/wiki/Abstraction" target="_blank" rel="noopener noreferrer">raciocínio abstrato</a> que utiliza princípios éticos universais. O consenso resultante é a ação tomada. O indivíduo age porque é correto e não porque evita a punição, é do seu interesse, é esperado, é legal ou foi previamente acordado.
                        <br />
                        <i>Por exemplo, Dani devia devolver a substância e deixar Marcos assumir toda a responsabilidade pelo que fez. Os estupefacientes arruinaram a vida de muitas pessoas. Não é correto apoiar o seu uso e propagação.</i>
                    </li>
                </ol>

