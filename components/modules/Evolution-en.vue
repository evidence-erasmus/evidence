                <p>
                    The module starts from an introductory video for opening up a few opinions about the Theory of Evolution. The aim of the video is to raise further questions rather than giving them immediate answers. It is expected that after watching <a href="https://www.youtube.com/watch?v=cXS_uSqzwuM" target="_blank" rel="noopener noreferrer">video 1</a> and/or <a href="https://www.youtube.com/watch?v=9jusKTXoQak" target="_blank" rel="noopener noreferrer">video 2</a> <b>(Activity 1)</b>, students in groups feel intrinsically motivated to find out more about the validity of the Theory of Evolution.  An intention is to involve students in committing to activities that relate to a better understanding of the issue – an issue seen by students as relevant to their lives, not simply relevant to the curriculum. It seeks to draw students’ attention to thinking about their prior knowledge and sharing their conceptions and views with peers.
                </p>
