                </UiContentImage>
                <p>Före och under hela testet bör eleverna uppmuntras att noggrant tänka igenom hur man kontrollerar de andra variablerna än maskens kvalité. Eleverna bör analysera vilka de potentiella felkällorna är, om och hur de kan undvikas, eller i efterhand efter experimentet, hur de kunde ha undvikits. I slutet av denna aktivitet förväntas det också att eleverna försöker hitta bevis från sekundära källor för att bekräfta sina egna fynd (de genomför även <nuct-link to="/viruses/student#activity-3ab">aktivitet 3b</nuct-link> i en förkortat form).</p>
                
                <h3>2. baserat på sekundära bevis</h3>
                <p>I de andra fallen kan de virusrelaterade myterna förmodligen inte motbevisas (prövas) experimentellt i klassrummet. Där förväntas eleverna samla bevis från sekundära källor för att bekräfta eller motbevisa sin hypotes/svar på sin forskningsfråga <b>(Aktivitet 3b)</b>. Genom att ställa datakällor mot varandra och deras innehåll, analyserar eleverna kritiskt deras tillförlitlighet och drar rimliga slutsatser, baserat på de bevis som hittats. Eftersom källkritik (tillförlitlighetsbedömning) kan vara en verklig utmaning för eleverna är elevmaterialet försett med verktyg som eleverna kan använda när de söker och analyserar information. Läraren kan också förklara hur det används genom att demonstrera och analysera både tillförlitliga och mindre tillförlitliga källor som inlärningsexempel innan eleverna börjar tillämpa detta verktyg på egen hand.</p>
                <p>För att lära sig att kommunicera sina resultat guidas eleverna att producera en video av deras experimentella resultat eller en video av deras slutsatser baserat på analys av sekundära källor <b>(Aktivitet 4)</b>. För detta ändamål kan eleverna använda en <a href="https://www.youtube.com/playlist?list=PLuC4DJElPQrOEMp2nLj1Q3h-_r8q7fzp0" target="_blank" rel="noopener noreferrer">videohandledning</a>. Alternativt kan eleverna göra en poster eller ett bildspel för att presentera sina resultat.</p>
                <p>Dessutom tillhandahålls elevmaterial med kriterier som videon (poster, etc) ska uppfylla. Dessa kriterier kan användas formativt för självbedömning av elever under processen, och för kamratbedömning när de presenterar videon (poster, etc) och svarar på frågor från sina kamrater och läraren <b>(Aktivitet 5)</b>. Slutligen kan eleverna använda följande <NuxtLink to="../tools/peer-feedback" target="_blank" class="btn btn-sm btn-accent text-white no-underline"><Icon name="mdi:tools" size="20"></Icon> </NuxtLink> för att ge feedback till de andra grupperna. Läraren kan ge varje grupp sammanfattande feedback på deras videopresentation (förslag: varje grupp kan bara ha tillgång till sin egen feedback, inte till de andras). Läraren kan enkelt komma åt feedbacken från Google Form genom att använda knappen "Svar". Gör en kopia av filen till samma mapp; efter det kan du dela länken till den kopierade filen med dina elever (inte originalfilen!).</p>
                <p>I den sista aktiviteten <b>(Aktivitet 6)</b> återgår berättelsen i modulen till introduktionsvideon och de presenterade myterna. Baserat på lärdomarna från de tidigare stadierna, förbereder sig eleverna på att fatta beslut på individ- eller gruppnivå m.h.a. av ett kortspel där de har möjlighet att jämföra och reflektera över sina kunskaper, men också över sina personliga och sociala värderingar. Det finns två teman som kan användas för att spela. Temat "Vaccination" är menat för elever som kan hantera mer information och överväga fler aspekter samtidigt, medan "Bära masker vid övre luftvägsinfektionspandemier" har mindre information att läsa igenom och kan användas av yngre elever eller elever med inlärningssvårigheter. Det finns också en möjlighet att läraren själv kan utelämna några berättelser eller informationskort.</p>
