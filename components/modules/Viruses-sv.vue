            <section id="s7">
                <h2 id="s7t">Undervisningsförslag</h2>

                <p>Modulen utgår från en introduktionsvideo för att öppna upp ämnet "Virus" och för att väcka ytterligare frågor hos elever istället för att ge dem omedelbara svar. Det förväntas att elever i grupper efter att ha sett <a href="https://youtu.be/t4v-36h2noo" target="_blank" rel="noopener noreferrer">videon</a> (<b>Aktivitet 1</b>) känner sig motiverade att undersöka virusrelaterade myter och föreställningar mer ingående. En avsikt är att engagera eleverna i aktiviteter som leder till en bättre förståelse av frågan – en fråga som eleverna ser som relevant för deras liv, inte bara relevant för kursplanen. Aktiviteterna  uppmuntrar eleverna att ta vara på sina förkunskaper och dela sina föreställningar och åsikter med klasskamrater <b>(Aktivitet 2)</b>. Inom Aktivitet 2 introduceras eleverna för 10 myter om virus, vaccination och maskbärande, från vilka de måste välja en för at analysera. Aktivitet 2 ska hjälpa eleverna att begränsa sin valda myt till en hypotes / forskningsfråga som kan motbevisas /bekräftas/besvaras <b>(Aktivitet 3)</b>. Det kan vara så att elever behöver hjälp med det. Därför skulle läraren kunna hjälpa dem med exempel på bra (t.ex. testbara) och dåliga hypoteser/frågor innan de själva ska formulera egna hypoteser. Följande <a href="https://www.thoughtco.com/testable-hypothesis-explanation-and-examples-609100" target="_blank" rel="noopener noreferrer">resurs</a> kan vara till hjälp för det.</p>
                <p><a href="https://youtu.be/eMAn2mfqR3A" target="_blank" rel="noopener noreferrer">"Faktavideon"</a> kan också demonstreras i detta steg för att påminna om de tidigare studerade (t.ex. tidigare under detta läsår eller tidigare läsår) virusrelaterade koncept och principer. Dessutom kan utvalda kapitel från <nuxt-link to="/viruses/background">vetenskaplig bakgrundsinformation</nuxt-link> användas för detta ändamål.</p>
                <p>Vidare kan <b>aktivitet 3</b> genomföras på två olika sätt:</p>

                <h3>1. baserat på experiment</h3>
                <p>Eleverna väljer en myt som kan testas experimentellt. Från de angivna myterna (se <nuct-link to="/viruses/student#activity-2">Aktivitet 2</nuct-link>) kan endast Myt 9 och 10 vara lämpliga för detta ändamål.</p>

                <h4 class="text-rose-400">Myt 9</h4>
                <p>Till exempel kan uppfattningen att <b>masker / munskydd är meningslösa för att förebygga sjukdomar i övre luftvägarna</b> (t.ex. COVID-19), utvecklas vidare till en testbar hypotes eller forskningsfråga. Det skulle vara bra att använda olika typer av masker (t.ex. textilmasker, N95, kirurgiska ansiktsmasker) i detta experiment för att få fler olika och därför intressanta resultat. Arbetsexempel på hur det kan förverkligas (operationaliseras) i klassrummet - kan direkt användas om eleverna är mindre erfarna i arbete med öppna frågor.</p>
                <p>Elever kan undersöka genomsläppligheten hos olika masker/ munskydd för att ta reda på vilken av de givna som är bäst för att blockera de små vätskedroppar i utandningsluft (eller inandad) genom vilken virus i övre luftvägarna vanligtvis brukar spridas. Små vattendroppar kan produceras, t.ex. via en vanlig sprayflaska, vilket gör det möjligt att relativt väl kontrollera mängden droppar vid varje sprayning. Detta antagande i sig kan testas med hjälp av elektroniska vågar - åtminstone om vikten av droppar förblir stabil under testerna.</p>
                <p>Vattnet kan sprayas genom varje maskmaterial samtidigt som sprayflaskan hålls direkt mot materialet. Resultaten kan övervakas genom att kvalitativt uppskatta intensiteten av droppströmmen som kommer genom materialet (eller inte). Ett annat alternativ kan vara att pappershandduksark hålls bakom masken på ett visst avstånd (eller direkt mot masken), och den våta fläcken på papper uppskattas och jämförs visuellt eller mäts direkt genom att väga papperets massa före och efter varje spray. De utspridda vattendropparna kan göras mer synliga på hushållspapper genom att istället använda bakpulverlösning, och hålla papper (sprayat tidigare med t.ex. fenolftaleinlösning, och torkat därefter), på ett visst avstånd från masken och sprayflaskan under besprutning.</p>
                <p>Den andra aspekten, som kan studeras kvalitativt, är hur mycket masken läcker från sidorna eller ovanför näsan eftersom det också har stor inverkan på dess effektivitet.</p>
                
                <h4 class="text-rose-400">Myth 10</h4>
                <p>Myt nr 10 (<b>”Långvarig användning av masken ger syrebrist (hypoxi)”</b>) kan testas, till exempel genom att använda fingerpulsoximeter före och efter långvarigt bärande av en mask. Pulsoximetrar (Fig. 2) kan köpas från olika nätbutiker, men också från lokala apotek och elektronikbutiker.</p>
