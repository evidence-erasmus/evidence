                    </li>
                    <li>
                        Selle suletud interaktsiooni tulemusena tekib populatsioonis  geenivoog (see tähendab, et erinevate isendite geenid ringlevad populatsioonis ja võivad kanduda edasi kogu populatsioonile).
                    </li>
                    <li>
