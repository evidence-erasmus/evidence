                <p>
                    Στην τελευταία δραστηριότητα (<b>Δραστηριότητα 6</b>), θα συζητηθούν κοινωνικοεπιστημονικά ζητήματα και αντιπαραθέσεις που σχετίζονται με την εξερεύνηση του διαστήματος <a href="http://www.encic.uma.es/" target="_blank" rel="noopener noreferrer"><sup><span class="tooltip" data-tip="Ο σχεδιασμός του παιχνιδιού ρόλων για ένα κοινωνικο-επιστημονικό πρόβλημα που σχετίζεται με το διάστημα ακολούθησε τη μεθοδολογία παιχνιδιού ρόλων που αναπτύχθηκε από τους εκπαιδευτικούς επιστήμης στο Πανεπιστήμιο της Μαλάγας http://www.encic.uma.es/">[1]</span></sup></a>. Για δεκαετίες, οι επιστήμονες και οι υπεύθυνοι χάραξης πολιτικής διαφωνούν για το αν η εξερεύνηση του διαστήματος ταιριάζει καλύτερα σε ανθρώπους ή ρομπότ (βλ. για παράδειγμα ένα πρόσφατο βιβλίο: «The End of Astronauts» των Donald Goldsmith και Martin, Rees Belknap Press, 2022). Στην τρέχουσα σκληρή οικονομική κατάσταση στην Ευρώπη αυτό το ζήτημα αποκτά ακόμη ένα λόγο για να μην πραγματοποιηθεί, καθώς οι επανδρωμένες διαστημικές πτήσεις είναι σημαντικά πιο ακριβές από τις ρομποτικές. Το προτεινόμενο <NuxtLink to="./space/student#s-6" target="_blank" rel="noopener noreferrer">παιχνίδι διαλογικής αντιπαράθεσης με ρόλους</NuxtLink> που παρουσιάζεται στο υλικό του μαθητή αφορά αυτό το θέμα.
                </p>

                <p>
