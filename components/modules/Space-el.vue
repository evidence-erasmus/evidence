                <h3>
                    2. Μέσω θεωρητικής έρευνας βασισμένη σε δευτερογενή στοιχεία
                </h3>

                <p>
                    Συχνά, οι μύθοι και οι παρανοήσεις που σχετίζονται με το διάστημα δεν μπορούν να καταρριφθούν (απομυθοποιηθούν) πειραματικά στην τάξη. Ως εκ τούτου, οι μαθητές αναμένεται να συγκεντρώσουν στοιχεία από δευτερογενείς πηγές για να επιβεβαιώσουν ή να απορρίψουν την υπόθεση/απαντήσουν στο ερευνητικό τους ερώτημα. Μέσω της αντιπαράθεσης και σύγκρισης των μέσων/πηγών δεδομένων και του περιεχομένου τους, οι μαθητές αναλύουν κριτικά την αξιοπιστία τους και βγάζουν δικαιολογημένα συμπεράσματα, με βάση τα στοιχεία που βρέθηκαν. Το υλικό του μαθητή παρέχει ένα εργαλείο που μπορεί να χρησιμοποιηθεί για την ανάλυση των πληροφοριών. Θα ήταν καλό ο δάσκαλος να μπορούσε να εξηγήσει τη χρήση του επιδεικνύοντας και αναλύοντας τόσο αξιόπιστες όσο και λιγότερο αξιόπιστες πηγές ως παραδείγματα προτού οι μαθητές αρχίσουν να εφαρμόζουν αυτό το εργαλείο μόνοι τους.
                </p>

                <p>
                    Προκειμένου να κοινοποιήσουν τα ευρήματά τους, οι μαθητές καλούνται να δημιουργήσουν μια οπτική παρουσίαση των συμπερασμάτων τους (<b>Δραστηριότητα 4</b>). Το βίντεο θα μπορούσε να είναι μια ενδιαφέρουσα επιλογή. Για το σκοπό αυτό, οι μαθητές μπορούν να συμβολευτούν το <a href="https://www.youtube.com/watch?v=_l8LgLhjLvc&list=PLuC4DJElPQrOEMp2nLj1Q3h-_r8q7fzp0" target="_blank" rel="noopener noreferrer">tutorial</a>. Εναλλακτικά, οι μαθητές θα μπορούσαν να φτιάξουν μια αφίσα ή μια διαφάνεια για να δείξουν τα ευρήματά τους.
                </p>

                <p>
                    Επίσης, το υλικό του μαθητή παρέχει κριτήρια (<NuxtLink to="./tools/peer-feedback" target="_blank">εργαλείο αξιολόγησης</NuxtLink>), που πρέπει να πληρεί η οπτική παρουσίαση όταν είναι έτοιμη. Αυτά τα κριτήρια μπορούν να χρησιμοποιηθούν για αυτοαξιολόγηση κατά τη διάρκεια δημιουργίας του βίντεο και για ετεροαξιολόγηση κατά την παρουσίαση των αποτελεσμάτων και στις απαντήσεις που δίνουν στις ερωτήσεις των συμμαθητών και του δασκάλου τους (<b>Δραστηριότητα 5</b>). Ο εκπαιδευτικός θα μπορούσε να δίνει συνοπτικά σχόλια σε κάθε ομάδα σχετικά με το βίντεο που δημιούργησαν (εισήγηση: κάθε ομάδα να έχει πρόσβαση μόνο στα σχόλια της δικής της παρουσίαση, όχι στις άλλες). 
