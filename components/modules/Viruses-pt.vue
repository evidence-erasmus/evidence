            </p>
            
            
            <h3>2. Secondary sources</h3>

            <p>
                Noutros casos, os mitos relacionados com o vírus não podem ser destruídos (desmascarados) experimentalmente na sala de aula. Por isso, espera-se que os estudantes reúnam provas de fontes secundárias para confirmar ou desmistificar a sua hipótese / resposta à sua questão de investigação (Atividade 3b). Ao justapor informação/fontes de dados e o seu conteúdo, os alunos analisam criticamente a sua fiabilidade e fazem conclusões justificadas com base nas provas encontradas. Como a avaliação da fiabilidade pode ser um verdadeiro desafio para os alunos, o material é fornecido com ferramentas para pesquisar e analisar a informação. Além disso, o professor poderá explicar a sua utilização ao demonstrar e analisar fontes fiáveis e menos fiáveis como exemplos de aprendizagem antes que os alunos implementem esta ferramenta. 
            </p>
            
            <p>
                Para aprender a comunicar as suas descobertas, os alunos são guiados a produzir um vídeo da sua experiência como prova ou a fazer um vídeo das suas conclusões com base em provas secundárias (<b>Atividade 4</b>). Para isso, os alunos podem utilizar um <a href="https://www.youtube.com/playlist?list=PLuC4DJElPQrOEMp2nLj1Q3h-_r8q7fzp0" target="_blank" rel="noopener noreferrer">tutorial de vídeo</a>. Em alternativa, os alunos podem fazer um cartaz ou um SlideShow para mostrar as suas descobertas.
            </p>
                
            <p>
                Além disso, o material do aluno é fornecido com critérios que o vídeo (cartaz, etc.) deve cumprir. Estes critérios podem ser utilizados formativamente para a autoavaliação por parte dos alunos durante o processo e para a avaliação pelos pares ao apresentar o vídeo (cartaz, etc.) e responder às perguntas dos seus pares e do professor (<b>Atividade 5</b>). Por último, os alunos podem usar a seguinte <NuxtLink to="../tools/peerfeedback" target="_blank" class="px-2 ring-2 ring-inset ring-blue-600 rounded inline-block no-underline hover:bg-yellow-100"><Icon name="mdi:tools" /> ferramenta de avaliação</NuxtLink> para dar feedback aos outros grupos. O professor poderia fornecer a cada grupo um feedback sumário sobre a sua apresentação em vídeo (sugestão: cada grupo poderia aceder apenas ao feedback da sua apresentação, não ao dos outros). O professor pode aceder facilmente ao feedback dos Formulários do Google recorrendo ao botão "Respostas".
