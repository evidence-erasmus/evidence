            <h2 id="section-7">Sugestões de ensino</h2>

            <p>
                O módulo começa com um vídeo introdutório para abrir o tema "Vírus" com o objetivo de levantar questões em vez de lhes dar respostas imediatas. Espera-se que depois de assistirem ao <a href="https://youtu.be/t4v-36h2noo" target="_blank" rel="noopener noreferrer">video</a> (<b>Atividade 1</b>), os alunos em grupos se sintam intrinsecamente motivados para examinar mitos e crenças relacionados com o vírus com maior profundidade. A intenção é envolver os alunos a comprometerem-se com atividades que se relacionem com uma melhor compreensão do tema – uma questão vista pelos alunos como relevante para as suas vidas, e não apenas para o currículo. Chama também à atenção dos alunos para que pensem nos seus conhecimentos prévios e partilhem as suas conceções e pontos de vista com os pares <b>(Atividade 2)</b>. No âmbito da Atividade 2, os alunos são introduzidos a 10 mitos sobre vírus, vacinação e uso de máscaras, dos quais devem escolher um para ser desmistificado. A atividade 2 deve ajudar os alunos a reduzir o seu mito escolhido numa hipótese/questão de investigação que pode ser refutada/confirmada/respondida <b>(Atividade 3)</b>. Pode ser que os alunos precisem de ajuda quando tentarem colocá-la. Portanto, o professor poderia ajudá-los dando exemplos de hipóteses/perguntas corretas (por exemplo, testáveis) e incorretas antes dos alunos as desenvolverem. O seguinte <a href="https://www.thoughtco.com/testable-hypothesis-explanation-and-examples-609100" target="_blank" rel="noopener noreferrer">recurso </a> pode ser útil para isso.
            </p>
            
            <p>
                O <a href="https://youtu.be/eMAn2mfqR3A" target="_blank" rel="noopener noreferrer">"vídeo sobre factos"</a> também pode ser demonstrado neste passo para recordar os conceitos e princípios anteriormente estudados (por exemplo, no início deste ano letivo ou nos anos letivos anteriores). Além disso, podem ser utilizados capítulos selecionados a partir de <NuxtLink :to="localePath('/viruses/background')">informações científicas de fundo</NuxtLink> para este fim.
            </p>

            <p>
                Seguidamente, a <b>Atividade 3</b> pode ser realizada de duas formas:
            </p>

            <h3>1. Experimental</h3>

            <p>
                Os alunos escolhem um mito que pode ser testado experimentalmente. A partir dos mitos dados (ver <NuxtLink :to="localePath('/viruses/student#activity-2')">Atividade 2</NuxtLink>), apenas os Mitos 9 e 10 poderiam ser adequados para este fim.
            </p>

            <h4>Mito nº 9</h4>

            <p>
                A crença de que <b>as máscaras são inúteis na prevenção de doenças respiratórias superiores</b> (por exemplo, COVID-19) pode ser desenvolvida para uma hipótese ou uma questão de investigação testável. Seria bom usar diferentes tipos de máscaras (por exemplo, máscaras têxteis, N95, máscaras faciais cirúrgicas) nesta experiência para obter resultados mais variados e emocionantes. Exemplos práticos de como podem ser operacionalizados na sala de aula - podem ser usados se os alunos são menos experientes com o formato de inquérito aberto (existem duas versões, uma aberta e a outra num formato de inquérito guiado / estruturado).
            </p>
            
            <p>
                Os estudantes podem explorar a permeabilidade de diferentes máscaras para descobrir o que, a partir do dado, é melhor para bloquear as pequenas gotas de líquido no ar expirado (ou inalado) pelo qual os vírus respiratórios superiores geralmente se espalham. O fluxo de gotículas de água minúscula pode ser produzido, por exemplo, através de uma garrafa de spray comum que permite a cada pressão controlar a quantidade das gotículas relativamente bem. Esta suposição pode ser testada com escalas eletrónicas - pelo menos, se o peso das gotículas permanecer estável durante os testes.
            </p>
            
            <p>
                A água pode ser pulverizada através de cada material de máscara enquanto segura a garrafa de spray diretamente contra o material. Os resultados podem ser monitorizados, estimando qualitativamente a intensidade do fluxo de gotícula que vem através do material (ou não). Outra opção pode ser manter o lenço de papel atrás da máscara a uma certa distância, (ou diretamente contra o material), e a mancha molhada no papel é estimada e comparada visualmente ou medida diretamente, pesando a massa do papel antes e depois de cada spray. As gotículas de água espalhadas podem ser tornadas mais visíveis no lenço de papel, utilizando água de bicarbonato de sódio e segurando papel (pulverizado anteriormente com, por exemplo, solução de fenolftaleína e seco depois disso) a uma certa distância da máscara e da garrafa de spray ao pulverizar.      
            </p>
            
            <p>
                O outro aspeto que pode ser estudado qualitativamente é a quantidade de fugas nas laterais ou acima do nariz, porque isso também afeta significativamente a sua eficiência. 
            </p>
            
            
            <h4>Mito nº 10</h4>
            
            <p>
                O mito nº 10 (<b>“O uso prolongado da máscara produz hipoxia”</b>) pode ser testado, por exemplo, para detetar medidas de oximetria no dedo antes e depois do uso prolongado de uma máscara. Os oxímetros de pulso (Fig. 2) podem ser comprados em lojas online e farmácias locais. Os seus preços começam a partir de alguns euros, embora deva ser aceite que os oxímetros mais baratos podem ser pouco fiáveis para obter resultados precisos.
            </p>
            
