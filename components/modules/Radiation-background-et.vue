            <h2 id="section-3">
                Hirmud tuumaenergia ees
            </h2>
            
            <p>
                Meie õpilased ei ole tavaliselt tuumaenergiast hästi informeeritud, kuid neil kipub esinema vaistlik hirm kiirguse ees. Sellel on ajaloolised juured. Tuumauuringud ja -arendus olid algusest peale sõjaväe kontrolli all ning suur osa teabest oli riikliku julgeoleku eesmärgil varjatud. Hiroshima ja Nagasaki tuumapommitamine ning II maailmasõja järgne konkurents Nõukogude ja USA vahel lõi aluse hirmule ja müstikale tuumaenergia ümber. Kuuskümmend aastat pärast seda, kui sellised katsed keelati, on atmosfääris tuumarelvade katsetamisest tulenev kiirgus endiselt õhus, mida me hingame.
            </p>
            
            <p>
                Veelgi enam, Nõukogude valitsuse poolt Tšernobõli tuumaelektrijaama kohta pärast õnnetust edastatud teabe puudumine, samuti pärast rahvusvahelist meediakampaaniat, mis esitas maailma üldsusele ülepaisutatud ohtude teateid, suurendas avalikkuse stressi ja muutis avalikku arvamust tuumaenergia suhtes veelgi negatiivsemaks. Uuringud näitavad, et "Tšernobõli mõju vaimsele tervisele on suurim õnnetuse põhjustatud rahvatervise probleem. … Need probleemid väljenduvad negatiivses enesehinnangus tervisele, usus lühenenud elueasse, algatusvõime puudumises ja sõltuvuses riigi abist”[32]. Teadaolevalt Tšernobõlist suure kiirgusdoosi saanud inimestest on vähktõve esinemissageduse kasv mõõtmiseks liiga väike, kuna see on tingitud muudest vähi põhjustest, nagu õhusaaste ja tubakatarbimine. Rahvatervise andmed mingil juhul ei kinnita vähi esinemist pärast kokkupuudet väikeste doosidega alla 100 mSv. Uuringud kutsetöötajatega, kes puutuvad pidevalt kokku normaalsest taustast kõrgema kiirgustasemega, ei ole näidanud kahjulikku bioloogilist mõju. Samas on madala doosiga kiirguse bioloogilised mõjud endiselt alauuritud. Vaistlikult eeldame, et igasugune kogus kiirgust võib kujutada endast teatud riski tekitada vähki ja põhjustada pärilikku mõju ning risk on suurem kiirgusega kokkupuute korral. Praegu hakkavad teadlased uuesti läbi vaatama kiirgusega kokkupuute mõjusid, mis viitavad sellele, et inimkeha suudab toime tulla väikese ja keskmise kiirgusdoosiga ilma märgatava tervise halvenemiseta. Telesaated, popkultuur ja isegi uudistereporterid kasutavad aga sageli sensatsiooni huvides ära puudulikku teavet või subjektiivseid arusaamu, kuna vaatajatele meeldib hea draama, mis toob kaasa liialdatud hirmu kiirguse ees – kiirgusefoobia. <sup><a href="https://www.nrc.gov/about-nrc/radiation/health-effects/rad-exposure-cancer.html" target="_blank" rel="noopener noreferrer">[33]</a></sup> 
            </p>
            

            <p>
                Olenevalt ajavarude olemasolust saab õpetaja kiirgusmooduliga tööd kitsendada tuumaenergia ja tuumakiirgusega seotud müütidega. Allpool esitame mõned müüdid, mida õpilasrühmad saavad omavahel jagada, et saada sügavamat ülevaadet ja koostada selle kohta videoselgitused (tegevused 2–5).
            </p>










