                <p>
                    Ühendkuningriigis on hinnatud radooni osakaalu aastases annuses ligikaudu poolele kogukiirgusest, kusjuures Saksamaal on radooni osakaal veerandi ringis. Teised suuremad panustajad on kosmiline kiirgus, maapealne gammakiirgus ja muude radionukliidide kui radooni allaneelamine (nt toiduna).
                </p>
                <h4>
                    Meditsiiniline kiirgus
                </h4>
                <p>
                    Inimtegevusest põhjustatud kiirguse osakaal on aastate jooksul suurenenud koos areneva meditsiinitehnoloogiaga. USA-s on meditsiinilistest allikatest pärit kiirguse osakaal kasvanud 1980. aastate alguse 15%-lt tänaseks 50%-ni. Suurem osa sellest on olnud tingitud kompuutertomograafias (CT) kasutatavast skaneerimisest, mis nõuab suuremaid kiirgusdoose kui traditsiooniline röntgenikiirgus. Kompuutertomograafia on röntgeniprotseduur, mis loob patsiendi siseorganitest ristlõikevaateid ja kolmemõõtmelisi kujutisi. Kui inimesel tehakse CT-skannimist, tehakse palju samaaegseid nn röntgenpilte. CT-skaneerimine loob siseorganitest väga selged pildid. Need üksikasjalikud pildid aitavad arstidel diagnoosida kehasiseseid probleeme, nagu kasvajad või elundikahjustused. CT-skaneerimine võib anda kirurgidele ka patsiendi nö sisemuse kaardi, mida nad saavad operatsiooni ajal jälgida. Kui inimesel on CT-skaneerimine, puutub ta kokku palju suurema kiirgusega kui tavaline röntgenikiirgus. Näiteks rindkere röntgenuuring annab 0,1 mSv, rindkere CT aga 7 mSv – 70 korda rohkem.
                </p>
                <p>
                    Kiirgust kiirgavad välised allikad ja inimene puutub sellega kokku ainult protseduuride ajal. Pärast seda pole neil aga radioaktiivset mõju.  <sup><a href="https://www.cdc.gov/nceh/radiation/x-rays.html" target="_blank" rel="noopener noreferrer">[20]</a></sup>. Siiski võtavad patsiendid <a href="https://www.regionaalhaigla.ee/et/nukleaarmeditsiini-osakond" target="_blank" rel="noopener noreferrer">nukleaarmeditsiini</a>  protseduuride ajal radioaktiivseid aineid, mida kasutatakse nende ravis või protseduuri läbiviimiseks. Kõigil radionukliididel (radioaktiivsetel isotoopidel) on "poolväärtusaeg", mis on aeg, mis kulub nende lagunemiseks, nii et patsiendid võivad pärast erinevat ravi jätkata tuvastatava kiirgustaseme kiirgamist erinevatel ajavahemikel. Tc-99m (tehneetsium-99m) on tuvastatav 3-4 päeva, fluor-18 (F-18), mis on kõige levinum radioisotoop, on tuvastatav ööpäeva jooksul, tehneetsium-99m (Tc-99m) ja tallium-201 (Tl-201) või nende kombinatsioon, mida kasutatakse müokardi perfusiooni (verevoolu) kuvamisel, on tuvastatav vastavalt mõne päeva ja kuni kuu jooksul. Jood-131 (I-131), mida kasutatakse hüpertüreoidismi, kilpnäärmevähi ja lümfoomi raviks, võib jääda tuvastatavaks kuni kolm kuud pärast ravi <sup><a href="https://www.snmmi.org/ClinicalPractice/content.aspx?ItemNumber=4825" target="_blank" rel="noopener noreferrer">[21]</a></sup>. Sel perioodil eraldub meie kehast väike kogus ioniseerivat kiirgust, seega võib öelda, et tegemist on radioaktiivse kiirgusega. Samas on kiirguse eralduvad kogused väikesed ja nukleaarmeditsiinil kui mitteinvasiivsel meetodil on võrreldes invasiivsete meetoditega mitmeid eeliseid.
                </p>
                <p>
                    Palju suuremaid annuseid antakse kiiritusravis, mida kasutatakse vähirakkude raviks kehas. Tüüpilised doosid on umbes 50 000 mSv, mis jagatakse mitmeks väiksemaks annuseks, mis antakse mõne kuu jooksul. Levinuim kiiritusravi liik on väliskiirte kiiritusravi, kus patsient saab kiiritusdoose meditsiiniseadmetest <sup><a href="https://www.cancer.net/navigating-cancer-care/how-cancer-treated/radiation-therapy/understanding-radiation-therapy" target="_blank" rel="noopener noreferrer">[18]</a></sup>. Pärast protseduuri kiirgust ei eraldu.
                </p>
                <p>
                    Teine protseduur on sisemine kiiritusravi, kui radioaktiivne materjal asetatakse implantaadi kujul vähki või seda ümbritsevasse koesse. Implantaadid võivad olla püsivad või ajutised.
                </p>
                <p>
                    Radioaktiivse aine lagunemisel kaotavad implantaadid aja jooksul radioaktiivsuse ja mitteaktiivsed kolded võivad jääda kehasse. Seetõttu ei tohiks patsient teatud aja jooksul (sageli kuni 2 kuud) puutuda lähedalt kokku laste või rasedatega rohkem kui 5 minuti jooksul.
                </p>
