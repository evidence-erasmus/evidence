            <section id="section-2">
                <h2 id="title-2">
                    Levinumad väärarusaamad, hirmud ja müüdid
                </h2>
                <p>
                    Viimastel aastatel on avalikkus hakanud kiirguse vastu üha enam huvi tundma <sup><a href="https://doi.org/10.1119/1.4893090" target="_blank" rel="noopener noreferrer" title="Neumann, S. (2014). Three Misconceptions About Radiation — And What We Teachers Can Do to Confront Them, Physics Teacher, 52, 357;">[9]</a></sup>. Mobiiltelefonid, moodsad mängukonsoolid ja WiFi on osa meie igapäevaelust. Inimesed puutuvad meedias sageli kokku arutelude ning väidetega, mis puudutavad erinevat tüüpi kiirgusi. Näiteks:  Kogu kiirgus on halb! Kas meditsiiniline kiiritamine on tõesti vajalik? Kas mobiiltelefonide kiirgus põhjustab vähki? Kas solaariumid tuleks keelata? Millised on tuumaelektrijaamade tuumareostuse tagajärjed?
                </p>
                <p>
                    Kuigi õpilased näivad sageli olevat väga motiveeritud kiirgusi tundma õppima, võib see tuua füüsikatundi sisse ka mitmed väärarusaamad. Mõned neist väärarusaamadest võivad olla põhjustatud kallutatud meediakajastustest, samas kui teised võivad olla tingitud sõna <i>kiirgus</i> igapäevasest tähendusest. <sup><a href="https://doi.org/10.1119/1.4893090" target="_blank" rel="noopener noreferrer" title="Neumann, S. (2014). Three Misconceptions About Radiation — And What We Teachers Can Do to Confront Them, Physics Teacher, 52, 357;">[10]</a></sup>
                </p>
                <h4>
                    Mure 5G pärast
                </h4>
                <p>
                    5G on lairiba-mobiilsidevõrkude viienda põlvkonna tehnoloogiastandard. See lubab andmeedastuskiiruste kümnekordset kasvu võrreldes praeguste 4G võrkudega, mis saavutatakse suurema edastussageduse kasutamisega.
                </p>
                <p>
                    Kõrgemate sageduste ja sellest tulenevalt lühema lainepikkuse tõttu nõuab 5G veidi tihedamat infrastruktuuri kui 3G või 4G kasutuselevõtt – mastid peaksid olema iga 100–200 meetri järel tihema tarbimisega aladel. Võrreldes varasemate tehnoloogiatega kasutab 5G kiirgusvihku moodustavaid antenne, et fokusseerida signaale tõhusamalt kasutatava seadme poole, selle asemel, et signaal leviks erinevates suundades nagu praeguste tugijaamade antennide puhul.
                </p>
                <p>
                    Kui sagedus suureneb, väheneb bioloogilistesse kudedesse tungimise sügavus, mistõttu nahk ja silmad muutuvad peamisteks tervisemõjude küsimuskohtadeks. Millimeeterlaineid neelavad enamasti nahk ja sarvkesta pinnakihid.
                </p>
                <p>
                    Mitmete ekspertorganisatsioonide sõnul ei viita miski sellele, et 5G millimeeterlainel oleks märkimisväärne terviserisk <sup><a href="http://emfhealth.info/research-expert-opinions.cfm" target="_blank" rel="noopener noreferrer">[11]</a></sup>, kuid paljud eksperdid ütlevad, et vaja on rohkem kvaliteetseid uuringuid. 5G kõrgriba sagedused on endiselt mitteioniseerivad, seega ei ole sellel kiirgusel piisavalt energiat, et lõhkuda DNA ja põhjustada rakkudes otseselt muutusi, mis võivad põhjustada vähki. Enamik seni avaldatud uuringuid traditsioonilise mobiiltelefoniteenuse kasutamise kohta raadiosagedusalas ei ole leidnud seost kasvajate tekkega <sup><a href="https://www.cancer.org/cancer/cancer-causes/radiation-exposure/cellular-phones.html" target="_blank" rel="noopener noreferrer">[12]</a></sup>. Kudede kuumutamine on raadiosagedusväljade ja inimkeha vahelise koostoime peamine mehhanism. Praeguste tehnoloogiate (2-, 3-, 4G) raadiosagedusliku kokkupuute tasemed põhjustavad inimkehas tühise temperatuuri tõusu (mõni kümnendik kraadi).
                </p>
                <p>
                    Mõned ülalnimetatud teadlased juhivad aga tähelepanu sellele, et 5G puhul ei ole küsimus mitte võimsuses, vaid impulsis, sageduses, millega kogu elanikkond tiheda antennivõrgu tõttu kokku puutub, ja hinnanguliselt miljardeid üheaegseid ühendusi. Uuringud näitavad, et impulss-elektromagnetväljad (EMF) on enamikul juhtudel bioloogiliselt aktiivsemad ja seetõttu ohtlikumad kui impulss-elektromagnetväljad <sup><a href="https://www.europarl.europa.eu/RegData/etudes/BRIE/2020/646172/EPRS_BRI(2020)646172_EN.pdf" target="_blank" rel="noopener noreferrer">[13]</a></sup>.
                </p>
                <p>
                    Seetõttu kahtlustavad mõned eksperdid, et nende seadmete kiirgus võib rakke kahjustada mõne muu bioloogilise mehhanismi kaudu, näiteks oksüdatiivse stressina rakkudes, mis põhjustab põletikku ja raku- ja koekahjustusi. On olnud arvamusi, et lühiajaline kokkupuude võib avaldada kahjulikku füsioloogilist mõju perifeersele närvisüsteemile, südameveresoonkonnale <sup><a href="https://blogs.scientificamerican.com/observations/we-have-no-reason-to-believe-5g-is-safe/" target="_blank" rel="noopener noreferrer">[14]</a></sup> ja immuunsüsteemile (mõjutab meie naha immuunvastust) <sup><a href="https://www.investigate-europe.eu/en/posts/5g-covid-conspiracy" target="_blank" rel="noopener noreferrer">[15]</a></sup>. Sellest hoolimata on need arvamused pärit väga piiratud arvust uuringutest, mis keskenduvad peamiselt WiFi sagedusaladele (2,4 GHz ja 5 GHz) <sup><a href="https://www.sciencedirect.com/science/article/pii/S0013935118300355" target="_blank" rel="noopener noreferrer">[16]</a></sup>
                </p>
                <h4>
                    Inimtekkeline kiirgus kui kiirgusega kokkupuute allikas
                </h4>
                <p>
                    Paljud inimesed arvavad, et kiirgus on otsene tehnoloogilise arengu tagajärg, mida toodavad elektriseadmed või et see pärineb tuumaelektrijaamadest. Inimesed eeldavad mõnikord, et ülekoormatud linna- ja tööstuspiirkondadest kaugel elamine vähendab oluliselt kiirgusega kokkupuudet või et mõni sajand tagasi ei olnud üldse kiirgust. Tegelikult pole see tõsi.
                </p>
                <p>
                    Asjaolu, et valgus ise on sarnaselt röntgenikiirguse või mikrolainetega samuti kiirgus, on õpilastel raske mõista. Valgus on nende arvates kiirgusest väga erinev: see on nähtav, samas kui inimesed peavad kiirgust tavaliselt nähtamatuks ja valgus näib olevat seotud eranditult positiivsete emotsioonidega. Nähtav valgus on mitteioniseeriv kiirgus, aga kuidas on lood ioniseeriva kiirgusega?
                </p>
                <p>
                    Välja arvatud võimalikud suuremahulised tuumaõnnetused, on looduslik ioniseeriv kiirgus suurim maailma elanikkonna kollektiivsesse efektiivdoosi panustaja. Inimkond puutub pidevalt kokku mitmest looduslikust allikast pärineva ioniseeriva kiirgusega, mille võib liigitada kahte suurde kategooriasse: suure energiaga kosmilised kiired (kosmiline panus); ja maakoores esinevad radioaktiivsed nukliidid (maapealne panus). Maapealne panuse annavad peamiselt uraani ja tooriumi lagunemisahelate radionukliidid koos radioaktiivse kaaliumiga. Enamikul juhtudel on radoon, uraani radioaktiivsel lagunemisel tekkiv väärisgaas, kõige olulisem kiirgusega kokkupuutumise põhjustaja. <sup><a href="https://ec.europa.eu/jrc/en/research-topic/natural-radiation" target="_blank" rel="noopener noreferrer">[17]</a></sup>
                </p>
