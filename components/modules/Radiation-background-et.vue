                    Tänapäeval on inimesed oma söödavast toidust teadlikumad kui kunagi varem ajaloos. Isegi kui toidu kiiritamise energiad on radioaktiivsuse esilekutsumiseks liiga väikesed, on mõned inimesed kiiritatud toidu suhtes ettevaatlikud. Kuigi toidu kiiritamine ei tekita toksikoloogilisi, mikrobioloogilisi ega toitumisprobleeme <sup><a href="https://academic.oup.com/cid/article/33/3/376/278043" target="_blank" rel="noopener noreferrer">[25]</a></sup>, vähendab see siiski vitamiinide taset toiduainetes, eriti B-rühma vitamiinide puhul. Samasugune vitamiinikadu võib aga toimuda ka traditsioonilise toiduvalmistamise ajal <sup><a href="https://academic.oup.com/cid/article/33/3/376/278043" target="_blank" rel="noopener noreferrer">[26]</a></sup>. Toidu kiiritamisel ei ole radioaktiivse saastatusega mingit pistmist, kuna toit ei puutu kiiritamise ajal kokku radioaktiivse materjaliga ega saa olla sel viisil saastunud.
