            <h2 id="section-4" class="mb-0">
                Tuumaenergia kohta levinud müütidega seotud faktid
            </h2>
            <h2 class="mt-2"><sup><a href="https://funginstitute.berkeley.edu/news/op-ed-lets-dispel-the-myths-about-nuclear-energy/" target="_blank" rel="noopener noreferrer">[34]</a></sup></h2>
            
            
            <h3><span class="text-rose-400">Müüt 1:</span> Tuumaenergia pole ohutu</h3>

            <p>
                Kolm tuumareaktoriõnnetust Three Mile Islandil, Tšernobõlis ja Fukushima Daiichis toimusid kokku 65 aastase perioodi jooksul. Nendest kolmest õnnetusest kahel ei olnud tuumakiirgusega seotud surmajuhtumeid ja Tšernobõlis oli umbes 50 otseselt tuumakiirgusega seotud surma <sup><a href="https://www.who.int/news/item/05-09-2005-chernobyl-the-true-scale-of-the-accident" target="_blank" rel="noopener noreferrer">[35]</a></sup>. Kuigi eel 4000 surmajuhtumit, mida põhjustas vähktõbi, võib kaudselt seostada Tšernobõli avariiga, on üldine surmajuhtumite arv madalam kui muude energiatootmisviiside puhul. "Tuumaenergiast põhjustatud surmajuhtumite arv on 442 korda väiksem kui pruunsöel energiaühiku kohta, isegi kui arvestada radioaktiivse kokkupuutega seotud surmajuhtumeid" <sup><a href="https://ourworldindata.org/what-is-thesafest-form-of-energy" target="_blank" rel="noopener noreferrer">[36]</a></sup>.”
            </p>

            <h3>
                <span class="text-rose-400">Müüt 2:</span> Tuumaenergia aitab kaasa süsinikdioksiidi heitkoguste suurenemisele ja põhjustab seega õhusaastet ja globaalset soojenemist
            </h3>

            <p>
                Tuumaenergia on vähese CO-heitega energiaallikas ja selle süsiniku jalajälg on üks väiksemaid. See toodab energiat tuumade lõhustumise, mitte keemilise põletamise teel, kuid erinevalt teistest vähese süsinikdioksiidiheitega meetoditest võib see tekitada ka suures koguses energiat. Lisaks ei sõltu tuumareaktorite toodetav energia ilmastikutingimustest, nagu taastuvad energiaallikad, lisaks võivad need aidata tõhusalt rahuldada kasvavat energianõudlust, mida nad suudavad toota väga väikesest kogusest uraanikütusest. Seetõttu esitletakse tuumaenergiat puhta ja tõhusa energiatootmise vormina, mis võib asendada fossiilkütuseid ning olla peamine energiaallikas ja taastuvenergia varuallikas.
            </p>

            <h3><span class="text-rose-400">Müüt 3:</span> Tuumajäätmeid tuleb ladustada 100 000 aastat</h3>

            <p>
                Enamik tuumakütust saab uuesti ringlusse võtta uue kütuse või kõrvalsaaduste saamiseks. Väike kogus tuumajäätmeid tuleb küll ladustada, kuid ladustamise aeg on alla 300 aasta  (mitte tuhandeid). Üle 90% tuumaelektrijaamades tekkivatest jäätmetest liigitatakse madala või keskmise radioaktiivsusega jäätmeteks. See hõlmab selliseid asju nagu vanaraud, paber, plast ja riided. Vähem kui 1% jäätmetest on radioaktiivne kuni 10 000 aastat. See osa ei ole palju radioaktiivsem kui mõned looduses leiduvad asjad ja seda saab inimeste ja eluslooduse kaitseks varjestada.
            </p>

            <p>
                Paljudes riikides on kavas jäätmete geoloogiline ladestamine, kui tuumajäätmed paigutatakse spetsiaalsetesse konteineritesse, mis seejärel asetatakse tunnelitesse ja võlvidesse sadade meetrite paksuse kivimi alla.
            </p>

            <h3>
                <span class="text-rose-400">Müüt 4:</span> Tuumajaamad eraldavad ohtlikus koguses kiirgust
            </h3>

            <p>
                Töötava tuumaelektrijaama teras- ja betoonkonstruktsioonid blokeerivad suurema osa tekitatud kiirgusest. Tuumaelektrijaamade kiirgust jälgitakse hoolikalt ja võrreldakse selle taset erinevatest allikatest pärineva taustkiirgusega. Päike ja tähed saadavad Maale pideva kosmilise kiirguse voo. Meile saadava kosmilise kiirguse kogus (või doos) sõltub kõrgusest, atmosfääritingimustest ja Maa magnetväljast. Kogu vesi sisaldab vähesel määral lahustunud uraani ja tooriumi ning kogu orgaaniline aine (nii taimne kui ka loomne) sisaldab radioaktiivset süsinikku ja kaaliumi. Mõned neist materjalidest neelatakse alla koos toidu ja veega, samas kui teised (näiteks radoon) hingatakse sisse.
            </p>



            <h3><span class="text-rose-400">Müüt 5:</span> Kokkupuude kiirgusega toimub ainult tuumaelektrijaamast</h3>

            <p>
                Inimesed, taimed ja loomad puutuvad alati kokku mingi kiirgusega. See tuleb maapinnast meie all ja ruumist meie kohal. Radioaktiivsed materjalid (sealhulgas uraan, toorium ja raadium) esinevad looduslikult pinnases ja kivimites. Niinimetatud taustakiirgus on osa meie igapäevaelust. Põhimõtteliselt sisaldab kogu õhk radooni, mis vastutab enamiku doosi eest, mida inimesed igal aastal looduslikest taustaallikatest saavad. Samuti kanname oma kehas väikeses koguses radioaktiivseid isotoope. Kuid meie keha on selle kiirgusega kohanenud ja meie rakud saavad sellega hakkama. Tuumatööstuse kiirgus on palju väiksem kui looduslikult esinev kiirgus.
            </p>


            <h3><span class="text-rose-400">Müüt 6:</span> Me ei vaja ELis tuumaenergiat</h3>

            <p>
                ELis on suur ja pidev nõudlus elektri järele. Tuule- ja päikeseenergia kõrval on tuumaenergia osa vähese CO₂-heitega elektri lahendusest. Prantsusmaal toodavad tuumajaamad ligi 80% elektrist. Kui tuule- ja päikeseenergia sõltuvad ilmast, siis tuumaenergia suudab alati energiat pakkuda. Pikemas perspektiivis saab tuumaenergiat kasutada kütuseelementides ja sünteetilistes vedelkütustes kasutatava vesiniku tootmiseks.
            </p>

            <h3><span class="text-rose-400">Müüt 7:</span> Tuumajaamad vananevad ja on ebatõhusad</h3>

            <p>
                Tehase seadmeid hinnatakse pidevalt, et teha kindlaks, mida on vaja uuendada või asendada. Tuumaelektrijaamad on seest kaasaegsed, sest enamik komponente on välja vahetatud.
            </p>

            <h3><span class="text-rose-400">Müüt 8:</span> Tuumaelektrijaamad võivad plahvatada</h3>

            <p>
                Kaasaegsel reaktoril on füüsiliselt võimatu plahvatada nagu tuumarelv. Kütuses ei ole plahvatusohtlikuks olemiseks piisavalt uraani ja reaktorid on projekteeritud turvasüsteemide kihtidega ja automaatse väljalülitusvõimega. Inimesel ei ole võimalik kaubanduslikku tuumareaktorit tahtlikult või tahtmatult muuta plahvatuse tekitamiseks.
            </p>

            <h3><span class="text-rose-400">Müüt 9:</span> Tuumajäätmeid ei saa ohutult transportida</h3>

            <p>
                Kasutatud kütust veetakse täna ohutult veoautode, raudteede ja kaubalaevadega. Tänaseks on spetsiaalselt selleks ette nähtud vaatides veetud tuhandeid saadetisi ilma ühegi kiirguslekketa.
            </p>

            <h3><span class="text-rose-400">Müüt 10:</span> Tuumajaama töötajad on ise radioaktiivsed.</h3>

            <p>
                Kõik inimesed saavad sünnist saati sisemist kiirgust, peamiselt radioaktiivsest kaalium-40-st ja süsinik-14-st, ning seetõttu on nad teiste kokkupuute allikad. Annuse kõikumine ühelt inimeselt teisele ei ole nii suur kui kosmiliste ja maapealsete allikate puhul. Tuumajaama töötajad ei ole teistest inimestest radioaktiivsemad.
            </p>


