            <h3>
                Mitteioniseeriv kiirgus
            </h3>

            <p>
                Kiirgus hõlmab nii madala sagedusega kiirgust kui ka mõõdukalt kõrge sagedusega kiirgust, sealhulgas raadiolaineid, mikrolaineid, infrapunakiirgust ja nähtavat valgust. Mitteioniseerival kiirgusel on piisavalt energiat, et molekulis aatomite ümber paigutada või neid vibreerima panna, kuid mitte piisavalt elektronide eemaldamiseks. Mitteioniseerivat kiirgust kasutatakse paljudes tavategevustes. Näiteks:
            </p>

            <ul>
                <li>
                    Mikrolainekiirgust telekommunikatsiooniks ja toidu soojendamiseks.
                </li>
                <li>
                    Infrapunakiirgust infrapunalampides toidu soojana hoidmiseks restoranides.
                </li>
                <li>
                    Raadiolaineid raadiolevi jaoks.
                </li>
            </ul>

            <p>
                Kõrgsageduslikud mitteioniseeriva ja ioniseeriva kiirguse allikad (nt Päike ja ultraviolettkiirgus) võivad üleliigse kokkupuute korral põhjustada põletusi ja kudede kahjustusi.
            </p>


            <h4>
                Elektromagnetväljad:
            </h4>
            <p>
                Elektromagnetväli  (sageli kasutusel ingl. k. lühend EMF) on elektrilaengute poolt tekitatav ja neid mõjustav füüsikaline väli, kusjuures elektri- ja magnetväli esinevad ühtse tervikuna. Elektromagnetväli levib ruumis elektromagnetlainena, milles elektriväli ja magnetväli perioodiliselt muutuvad. Need energiaväljad ümbritsevad meid pidevalt. Teaduslikud uuringud ei ole selgelt näidanud, kas kokkupuude elektromagnetväljadega suurendab terviseriski. Mõned uuringud on seostanud elektromagnetvälja ja tervisemõjusid, kuid neid tulemusi pole õnnestunud korrata. See tähendab, et need on ebaselged. Teadlased jätkavad selle teema uurimist.
            </p>

            <p>
                Elektrivälja tekitavad elektrilaengud ja magnetvälja tekitab juhtmeid või elektriseadmeid läbiv elektrivool. EMF-i tugevus on võrdeline elektriliini läbiva voolu tugevusega ja väheneb kauguse kasvades.
            </p>
            
            <h4>
                Elektromagnetkiirgus juhtmevabast tehnoloogiast
            </h4>
            
            <p class="mt-0">
                Elektroonikaseadmed, mis saadavad raadiosagedusel (ingl. k. radio frequency, RF) õhu kaudu elektromagnetilisi signaale, on kõikjal: Wi-Fi ruuterid, mobiiltelefonid, raadiod ja muud võrgud. Nad edastavad teavet raadiolainete kujul. Oleme pidevas elektromagnetilise kiirguse pilves. Mõned inimesed on mures juhtmevaba tehnoloogia elektromagnetilise kiirguse võimalike tervisemõjude pärast. Mobiiltelefonid ja traadita võrgud saadavad raadiosagedusel elektromagnetilist kiirgust energiatasemel, mis ei põhjusta märkimisväärset kuumenemist. Enamik uuringuid ei ole leidnud mobiiltelefoni kasutamise tervisemõjusid. Mõned uuringud on seostanud raadiosageduslikku mõju tervisele, kuid teadlased ei ole suutnud tulemusi korrata. See tähendab, et need on ebaselged. <sup><a href="ttps://www.epa.gov/radtown/electric-and-magnetic-fields-power-lines#what-you-can-do" target="_blank" rel="noopener noreferrer">[2]</a></sup>.
            </p>

            <p>
                Mobiiltelefonide raadiolainete sageduskiirgus ei ole piisavalt tugev, et mõjutada otseselt aatomite struktuuri või kahjustada DNA-d, aga see põhjustab aatomite vibratsiooni, mis võib omakorda põhjustada aine/keha kuumenemist. Kui raadiolainete energia on väga tugev, näiteks radarisaatjatest, võib see olla ohtlik. See võib inimkeha osi väga kiiresti kuumutada ja põhjustada tõsiseid vigastusi, näiteks tõsiseid põletusi. Raadiosageduslik energia väheneb atmosfääris liikudes, mis tähendab, et mida kaugemal see saatjast on, seda nõrgemaks see läheb. Mobiiltelefonitornid võivad edastada kõrgemat RF-energia taset, seega on neil hoiatused, mis piiravad juurdepääsu tornile endale või ümbritsevale alale.
                <sup><a href="ttps://www.epa.gov/radtown/non-ionizing-radiation-wireless-technology#about-non-ionizing-radiation" target="_blank" rel="noopener noreferrer">[3]</a></sup>.
            </p>

            <p>
                Mobiilsidetehnoloogiad (nt mobiiltelefonid) edastavad ja võtavad vastu raadiosageduslikke elektromagnetvälju (RF-EMF) viisil, mis võimaldab suhtlemist. Spetsiifilist raadiosageduslike elektromagnetväljade kasutamise meetodit nimetatakse "juhtmevabaks standardiks". Näiteks mobiilside esimese põlvkonna traadita side standardit nimetatakse 1G-ks, samas kui on tekkinud viienda põlvkonna traadita side standard, mida tuntakse kui 5G.
            </p>

            <h4>
                Raadiosageduslike magnetväljade mõju kehale.
            </h4>

            <p class="mt-0">
                Juhtmevaba tehnoloogia elektromagnetkiirgus võib tungida läbi inimkeha, mille peamiseks tagajärjeks on temperatuuri tõus eksponeeritud koes. Inimkeha suudab kohaneda väikese temperatuuritõusuga samamoodi, nagu trenni tehes, vannis või saunas käies. Seda seetõttu, et keha suudab reguleerida oma sisetemperatuuri.
            </p>
            <p>
                Teine aspekt on see, et mida kõrgem on sagedus, seda väiksem on juhtmevaba tehnoloogia elektromagnetkiirguse kehasse tungimise sügavus. Kuna 5G tehnoloogiad kasutavad kõrgemaid sagedusi (>24 GHz), neeldub kiirgus pigem pindmiselt (ja vähem ajukoesse sattudes) kui varasemate mobiilsidetehnoloogiate (4G jne) puhul. Kuigi pinnapealselt (vastupidiselt sügavamale keha kudedesse) neelduva kiirguse võimsuse osakaal on kõrgemate sageduste puhul suurem, on ICNIRP (2020) seadnud piirangud tagamaks, et ruumiline tippvõimsus jääks palju madalamaks sellisest kiirgusest, mis mõjutaks tervist kahjulikult. Sellest tulenevalt ei põhjusta 5G kokkupuude mingit kahju, kui see järgib ICNIRP (2020) juhiseid. 5G kasutamisega seotud murekohtade tõstatamine ei ole uus asi, sama toimus ka 1G, 2G, 3G ja 4G puhul. Vaatamata tõstatatud hirmudele ei ole ajuvähi esinemissagedus märkimisväärselt muutunud, kui liikuda ajas  80ndate telefonide mittekasutamise perioodisttänapäeva lääneriikide elanikkonna valdava kasutamiseni. <sup><a href="https://www.icnirp.org/en/applications/5g/index.html" target="_blank" rel="noopener noreferrer">[4]</a> <a href="https://www.icnirp.org/en/activities/news/news-article/rf-guidelines-2020-published.html" target="_blank" rel="noopener noreferrer">[5]</a> <a href="https://www.sciencealert.com/what-s-5g-and-why-are-people-so-scared-of-it-here-s-what-you-need-to-know" target="_blank" rel="noopener noreferrer">[6]</a></sup>
            </p>



            <h4>
                Kasutamine
            </h4>

            <p>
                Mikrolaineahjud kasutavad elektromagnetilist kiirgus toidu soojendamiseks. Mikrolaineahjus kasutatav mitteioniseeriv kiirgus ei muuda toitu radioaktiivseks. Mikrolaineahju toodetakse ainult siis, kui ahi töötab. Ahjus toodetud mikrolained neelavad toitu ja toodavad kuumust, mis toidu küpsetab. Mikrolaineahjud on konstrueeritud nii, et elektromagnetkiirgus ei lahkuks ahjust. <sup><a href="https://www.epa.gov/radtown/non-ionizing-radiation-used-microwave-ovens " target="_blank" rel="noopener noreferrer">[7]</a></sup>
            </p>


            <h3>
                Ioniseeriv kiirgus
            </h3>

            <p>
                Ioniseeriv kiirgus hõlmab kõrgema sagedusega elektromagnetkiirgust: ultraviolettkiirgust, röntgeni- ja gammakiirgust. Ioniseerival kiirgusel on piisavalt energiat, et lõhkuda molekulides keemilisi sidemeid või eemaldada aatomitelt elektrone, tekitades laetud molekule või aatomeid (ioone).
            </p>

            <p>
                Ioniseeriv kiirgus võib kahjustada eluskudesid, muutes rakustruktuuri ja kahjustades DNA-d. Kahju suurus sõltub kiirguse liigist, kokkupuute rajast, kiirguse energiast ja neeldunud kiirguse koguhulgast. Väikese või isegi mõõduka kokkupuute mõju ei pruugi olla märgatav. Enamik rakukahjustusi on paranduvad, kuid mõned rakud ei pruugi taastuda nii hästi kui teised ja võivad kahjustuda või muutuda vähkkasvajaks. Kiirgus võib ka rakke tappa. Lapsed on ioniseeriva kiirguse suhtes tundlikumad kui täiskasvanud, sest lapsed on alles kasvamisjärgus. Seal on rohkem rakke jagunemas ja suurem võimalus kiirgusel kasvuprotsessi häirida.
            </p>

            <p>
                <span class="e-term font-serif">Siivert</span> (sümbol: <b>Sv</b>) on ioniseeriva kiirguse doosi ühik rahvusvahelises mõõtühikute süsteemis (SI) ja see mõõdab ioniseeriva kiirguse tervisemõju inimorganismile. Siivert on oluline dosimeetrias ja kiirguskaitses ning on oma nime saanud Rootsi meditsiinifüüsiku Rolf Maximilian Sieverti järgi, kes on tuntud kiirgusdoosi mõõtmise ja kiirguse bioloogiliste mõjude uurimise eest.
            </p>

            <h4>
                Ioniseeriv kiirgus ja vähk
            </h4>

            <p>
                Ioniseeriv kiirgus ja vähk. Uuringud enam kui 100 000 Jaapanis Hiroshima ja Nagasaki aatomipommidest ellujäänuga Teise maailmasõja lõpus näitavad:
            </p>

            <ul>
                <li>
                    Vähki haigestumise võimalus suureneb kiirgusdoosi suurenedes.
                </li>
                <li>
                    Kiiritusest põhjustatud vähid ilmnevad alles aastaid pärast kiirgusega kokkupuudet.
                </li>
                <li>
                    Mõnel inimesel tekib kiirgusega kokkupuutest suurem tõenäosus haigestuda vähki kui teistel.
                </li>
            </ul>


            <h4>
                Ultraviolettkiirgus (UV-kiirgus) ja sellega kokkupuude
            </h4>

            <p>
                Päike saadab Maale energiat mitmel erineval viisil: nähtav valgus, infrapunakiirgus, mida tunneme soojusena ja UV-kiirgus, mida me otseselt ei näe ega tunne. UV-kiired põhjustavad naha vananemist ja silmakahjustusi ning võivad vähendada keha võimet haigustega võidelda. Lühema lainepikkusega UV-kiirgus (nn UVB) põhjustab päikesepõletust ja nahavähki. Nahavähki haigestumine EL-is ulatub igal aastal 90 000 uue juhtumini, neist üle poole Rootsis. See on üks kiiremini arenevaid vähivorme. <sup><a href="https://ec.europa.eu/research/health/pdf/factsheets/melanoma_skin_cancer.pdf" target="_blank" rel="noopener noreferrer">[8]</a></sup>. 
            </p>

            <p>
                Kuigi UV-kiirguse eest peaksid ennast kaitsma kõik, on eriti oluline kaitsta lapsi. Liiga palju UV-kiirgust, sh saadud sagedased päikesepõletused ning eriti lapsepõlves, võivad muuta nahavähi tekke tõenäolisemaks.
            </p>













