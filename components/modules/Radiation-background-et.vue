                    Päevitamine
                </h4>
                <p>
                    Mõned siseruumides päevitamise lobirühmad väidavad, et kuna päevitusaparaadid kasutavad enamasti UVA-valgust, on sel viisil päevitamine ohutum kui päikese käes lamamine. See ei ole tõsi. Ultraviolettkiirgus (UV) liigitatakse UVA-kiirguseks (lainepikkus 320-400 nm), UVB-kiirguseks (290-315 nm) ja UVC-kiirguseks (100-280 nm). Aastaid on teadlased uskunud, et UVB-kiired on kahjulikumad, kuna need põhjustavad päikesepõletust, mis on seotud riskiga saada melanoom. Praeguseks teame, et UVA-kiired kutsuvad esile fotovananemise ja suurendavad samuti nahavähi riski <sup><a href="https://www.health.harvard.edu/staying-healthy/is-a-tanning-bed-safer-than-sunlight" target="_blank" rel="noopener noreferrer">[27]</a></sup>. UVA-kiired, kuigi veidi vähem intensiivsed kui UVB, tungivad nahka sügavamale. <sup><a href="https://www.health.harvard.edu/staying-healthy/is-a-tanning-bed-safer-than-sunlight" target="_blank" rel="noopener noreferrer">[28]</a></sup> Kokkupuude põhjustab geneetilisi kahjustusi naha pealmise kihi sisemises osas, kus esineb enamik nahavähkidest. Nahk püüab vältida edasisi kahjustusi tumenemise läbi, mille tulemuseks on päevitus. UVA-kiirgus on peamine valguse liik, mida kasutatakse enamikus solaariumides. Kunagi arvati, et see on ohutu, teame nüüd, et see on just vastupidi võrreldes loomuliku päevitamisega.  Juba üks päevitussessiion siseruumides võib suurendada riski haigestuda nahavähki (melanoom 20%, lamerakk-kartsinoom 67% ja basaalrakuline kartsinoom 29%) <sup><span class="tooltip" data-tip="Wehner MR, Chren M, Nameth D, et al. International Prevalence of Indoor Tanning: A Systematic Review and Meta-analysis.JAMA Dermatol. 2014;150 (4): 390-400. doi:10.1001/jamadermatol.2013.6896."><span class="text-blue-600">[29]</span></span></sup>. Ameerika Oftalmoloogia Akadeemia on teatanud, et solaariumid toodavad 100 korda intensiivsemat UV-kiirgust kui see esineb päikese käes <sup><a href="https://www.aao.org/eye-health/tips-prevention/indoor-ta