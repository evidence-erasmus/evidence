                <h4>Müüt nr 9</h4>
                <p>Näiteks uskumust, et <b>maskidest pole mingit kasu ülemiste hingamisteedega seotud nakkushaiguste (N: COVID-19) ärahoidmisel / piiramisel</b>, saab edasi arendada testitavaks hüpoteesiks või uurimisküsimuseks. Et saada huvitavamaid tulemusi, võiks uurimisobjektina  kasutada võimalikult erinevat tüüpi maske (erinevaid tekstiilist maske, N95 maski, kirurgilisi maske ja võib-olla ka lihtsalt torukujulisi salle, mida kasutatakse mõnikord maski asemel). Järgnevalt on toodud ära näited, kuidas selline uurimistöö võiks kulgeda. Üks katsete läbiviimise juhend on toodud <a href="https://docs.google.com/document/d/1SFKk3aMaLHD_Pf095JQsMDw631gkgVyGzbtsDxxbnsA/edit#heading=h.s6wd1rahy8uo" target="_blank" rel="noreferrer noopener">juhendatud</a> ning teine <a href="https://docs.google.com/document/d/1SFKk3aMaLHD_Pf095JQsMDw631gkgVyGzbtsDxxbnsA/edit#heading=h.imry5ddx56jf" target="_blank" rel="noopener noreferrer">struktureeritud uurimistöö</a> vormis. Antud töölehti saab kasutada ka otseselt uurimistöö läbiviimiseks juhul, kui õpilased on vähem kogenud uurimisküsimuste ja hüpoteeside püstitamises ning katse planeerimises. Muudel juhtudel peaksid ikkagi õpilased ise (või õpetaja väikese kaasabiga) olema võimelised uurimistöö kavandama ja ellu viima (mitte pelgalt näpuga juhiseid järgima).</p>
                <p>Õpilased võiksid uurida erinevate maskitüüpide läbitavust, et leida, millised neist on tõhusaimad ülipeente veepiiskade varjestamisel. Pihustatavad veepiisad simuleerivad viisi, kuidas nakkused ülemiste hingamisteede kaudu levivad. Pihustada saab näiteks tavalise tarbekaupade kauplustes müüdava pihustuspudeli abil, mis võimaldab suhteliselt hästi kontrollida iga pressiga väljastatavat vedeliku kogust. Eeldust, et pihustatavad kogused on enam-vähem võrdsed, saab samuti kontrollida elektronkaalu abil viies selleks läbi seeria katseid.</p>
                <p>Vett võiks pihustada läbi maski materjali hoides pudeli pihustusava otse vastu maski. Tulemusi saab hinnata puhtalt vaatluse abil, st mil määral vedelikupiisad läbivad maski (või siis mitte) - näiteks vaadelda vedelikupiiskade liikumist küljelt. Teine võimalus on hoida majapidamispaberit iga katse käigus kindlal kaugusel maskist või vahetult maski taga ning hiljem võrrelda läbinud vedelikupiiskade poolt tekitatud niiskeid laike omavahel visuaalselt (sel juhul tuleks katsed teha võimalikult kiiresti üksteise järel, sest vedelik hakkab kiiresti imbuma paberkäterätiku materjali ning seega pole suurte vahedega tehtud katsete tulemused selliselt võrreldavad). Muidugi on võimalik  mõõta ka paberi poolt kinnipüütud vee massi elektronkaaludega (kaaludes paberit enne ja pärast pihustamist). Pihustatud veetilgad saab muuta paberil nähtavamaks, kui kasutada söögisooda lahust ning pihustada  paberkäterätiku lehed eelneavalt fenoolftaleiini lahusega üle.</p>
                <p>Maski lekkimine külgedelt võib samuti olla uurimis- ja kaalutlemisobjektiks, kuna antud asjaolu mõjutab oluliselt maski tõhusust.</p>
                
