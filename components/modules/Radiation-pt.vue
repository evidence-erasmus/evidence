            <h2 id="section-7">Sugestões de ensino</h2>

            <p>
                O trabalho com este módulo pode ser estruturado em duas partes. A primeira parte (Atividades 1-5) está principalmente relacionada com o trabalho com mitos específicos da radiação identificados no vídeo introdutório, recordado pelos alunos, ou sugerido pelo professor. A segunda parte (Atividade 6) centra-se no trabalho com o tópico sócio agudo da Energia Nuclear que é resumido com a ajuda de um jogo de debate /role-play (ver material do aluno).
            </p>

            <p>
                O módulo começa com um vídeo introdutório para levantar algumas questões e revelar medos, mitos ou equívocos sobre a radiação, por exemplo:
            </p>

            <ul>
                <li>Telemóveis emitem radiação eletromagnética perigosa</li>
                <li>5G não é seguro</li>
                <li>Luvas de borracha podem proteger-nos de radiações eletromagnéticas emitidas por telemóveis</li>
                <li>Radiação eletromagnética (ondas de rádio) pode ser "congelada" a baixa temperatura</li>
                <li>As ondas eletromagnéticas podem, efetivamente, ser anuladas pelas paredes de um frigorífico.</li>
                <li>Folha de plástico e alumínio pode bloquear ondas eletromagnéticas</li>
                <li>Cabos elétricos emitem radiação eletromagnética perigosa</li>
                <li>O sol envia radiações perigosas</li>
            </ul>

            <p>
                O <a href="https://www.youtube.com/watch?v=ccMo9WfKeOY" target="_blank" rel="noopener noreferrer">vídeo introdutório</a> tem como objetivo levantar questões e estimular reflexões em vez de dar respostas imediatas aos alunos. Espera-se que depois de assistirem ao vídeo <b>(Atividade 1)</b>, os alunos desenvolvam motivação para investigar um dos mitos identificados relacionados com diferentes fontes de radiação eletromagnética e explorar medos comuns. Os mitos e os medos podem variar entre diferentes países e faixas etárias. Os professores podem incentivar os alunos a encontrar os seus próprios mitos.
            </p>

            <p>
                O professor pode introduzir uma visão expandida do espectro de radiação que inclui componentes ionizantes – desde o ultravioleta emitido pelo nosso Sol até ao comprimento de onda mais curto e partículas de alta energia. A intenção é alargar a opinião dos alunos sobre a física da radiação e as suas aplicações e atrair a sua atenção para alguns mitos relacionados com os diferentes efeitos da radiação. Os alunos são também encorajados a recordar os seus conhecimentos anteriores, incluindo os entendimentos existentes no dia-a-dia entre pessoas que conhecem, família e amigos, e a partilhar as suas conceções e pontos de vista com os pares (Atividade 2). Os "vídeos de factos" – podem ser demonstrados neste passo para destacar e alargar temas, conceitos e princípios relacionados com a radiação. Os capítulos selecionados a partir de informações científicas de fundo – Ver Documento <nuxt-link to="./radiation/background">“Informação científica”</nuxt-link> podem repetir-se quando for necessário.
            </p>



