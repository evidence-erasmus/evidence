            <h2>2. Ποιες είναι οι επιπτώσεις της Κλιματικής Αλλαγής;</h2>
            
            <p>Η Διακυβερνητική Επιτροπή για την Κλιματική Αλλαγή (IPCC), η οποία περιλαμβάνει περισσότερους από 1.300 επιστήμονες από τις Ηνωμένες Πολιτείες, αναφέρει ότι το μέγεθος των επιπτώσεων της κλιματικής αλλαγής σε μεμονωμένες περιοχές θα διαφέρει με την πάροδο του χρόνου και θα ποικίλει ανάλογα με την ικανότητα διαφορετικών κοινωνικών και περιβαλλοντικών συστημάτων να μετριάσουν ή προσαρμοστούν στην αλλαγή.</p>
            
