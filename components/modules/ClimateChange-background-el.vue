            <h2>3. Κλιματική Αλλαγή: Ένα κοινωνικο-επιστημονικό ζήτημα και αμφιλεγόμενο ζήτημα</h2>

            <p>Στην ιστορία της Επιστήμης υπήρξαν και υπάρχουν πολλά παραδείγματα όπου αντιφατικές απόψεις για συγκεκριμένα επιστημονικά ζητήματα διαδόθηκαν στον γενικό πληθυσμό. Ένα παράδειγμα αμφιλεγόμενου θέματος είναι η κλιματική αλλαγή.</p>

            <p>Παρόλο που η κλιματική επιστήμη έχει πλέον αποδείξει ακράδαντα ότι συμβαίνει υπερθέρμανση στον πλανήτη, ότι οι ανθρώπινες δραστηριότητες συμβάλλουν σε αυτή την υπερθέρμανση και ότι η τρέχουσα και η μελλοντική υπερθέρμανση προμηνύουν αρνητικές επιπτώσεις τόσο στα οικολογικά όσο και στα κοινωνικά συστήματα (Εθνικό Συμβούλιο Έρευνας, 2010), ένα σημαντικό μέρος του αμερικανικού κόσμου παραμένει αντιμαχόμενο ή δεν ανησυχεί (Leiserowitz, Maibach, Roser-Renouf, & Hmielowski, 2012). Πολλοί υπεύθυνοι χάραξης πολιτικής (ειδικά στις Ηνωμένες Πολιτείες) αρνούνται την ανάγκη λήψης μέτρων για τη μείωση των εκπομπών άνθρακα (Brownstein, 2010).</p>

            <p>Οι αρνητές της κλιματικής επιστήμης δεν δέχονται στοιχεία που υποστηρίζουν την ύπαρξη κλιματικής αλλαγής, τον ρόλο της ανθρωπότητας στην πρόκληση της πρόσφατης υπερθέρμανσης του πλανήτη ή/και τη σοβαρότητα των κλιματικών επιπτώσεων. Σκεπτικισμός (scepticism), αντιφατικότητα (contrarianism), απόρριψη (dismissal), διαφωνία (dissent), αμφιβολία (doubt) ή κατά της κλιματικής αλλαγής (anti-climate change) είναι μερικοί από τους όρους που έχουν χρησιμοποιηθεί για να χαρακτηρίσουν τις αρνήσεις της κλιματικής επιστήμης (Cook, 2017).</p>

            <p>Η παραπληροφόρηση για το κλίμα είναι ο λόγος για τις λανθασμένες αντιλήψεις του κοινού σχετικά με την κλιματική αλλαγή (McCright and Dunlap, 2010). Προκύπτουν ερωτήματα όπως: (1) Η αύξηση της θερμοκρασίας της Γης προκαλεί αύξηση του διοξειδίου του άνθρακα ή η αύξηση του διοξειδίου του άνθρακα προκαλεί αύξηση της θερμοκρασίας της Γης; (2) Οι ανθρώπινες δραστηριότητες παράγουν ίσες, λιγότερες ή περισσότερες ποσότητες διοξείδιο του άνθρακα από τις φυσικές διεργασίες; (3) Αλλάζει το κλίμα με την πάροδο του χρόνου; Εάν ναι, είναι η κλιματική αλλαγή μια προσωρινή αλλαγή όπως οι προηγούμενες; (4) Οι μαζικές εξαφανίσεις ειδών σήμερα είναι αποτέλεσμα κλιματικής αλλαγής ή συνηθισμένο φαινόμενο με τα χρόνια;</p>

            <p>Τα μέσα μαζικής ενημέρωσης διαδραματίζουν κεντρικό ρόλο στις συζητήσεις για την επιστήμη του κλίματος και τη χάραξη πολιτικής. Ο Maxwell Boykoff, στη μακρόχρονη πείρα του, αναλύει τους τρόπου με τους οποίους τα μέσα παρουσιάζουν την κλιματική αλλαγή και συμβάλλουν στις «απομακρυσμένες φωνές», αφού σκεπτικιστές και αρνητές λαμβάνουν αδικαιολόγητη προβολή στα μέσα ενημέρωσης και συνεπώς επηρεάζουν τις συζητήσεις πολιτικής.</p>


